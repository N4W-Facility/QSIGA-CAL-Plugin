    def populate_points(self, series_path):
        """
        Extract unique point names from series files (before _ in filename).
        """
        try:
            self.listWidget_points.clear()

            # Get all txt files in series folder
            series_files = [f for f in os.listdir(series_path) if f.endswith('.txt')]

            if not series_files:
                self.textEdit_export_log.append("⚠ No series files found")
                return

            # Extract unique point names (part before _valor or _media)
            points = set()
            for filename in series_files:
                # Remove .txt extension
                name = filename.replace('.txt', '')
                # Remove _valor or _media suffix
                if name.endswith('_valor') or name.endswith('_media'):
                    point_name = name.rsplit('_', 1)[0]
                    points.add(point_name)

            # Add points to list widget
            for point in sorted(points):
                self.listWidget_points.addItem(point)

            self.textEdit_export_log.append(f"✓ Found {len(points)} point(s)")

        except Exception as e:
            self.textEdit_export_log.append(f"⚠ Error extracting points: {str(e)}")
            QgsMessageLog.logMessage(f"Error extracting points: {str(e)}", 'siga_step_05_Sim', Qgis.Warning)

