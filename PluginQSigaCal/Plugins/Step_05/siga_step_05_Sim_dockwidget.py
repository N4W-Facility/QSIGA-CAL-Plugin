    def export_results_to_excel(self):
        """
        Export selected points and variables to Excel files.
        Creates separate files for _valor and _media conditions.
        Each Excel file has one sheet per variable, with columns for each point.
        """
        try:
            import pandas as pd
            from ..common_functions.spotpy_setup_SIGA import ReadTimeSeries

            # Validate selections
            selected_points = [item.text() for item in self.listWidget_points.selectedItems()]
            selected_variables = [item.text() for item in self.listWidget_variables.selectedItems()]

            if not selected_points:
                QtWidgets.QMessageBox.warning(self, "Selection Error", "Please select at least one point")
                return

            if not selected_variables:
                QtWidgets.QMessageBox.warning(self, "Selection Error", "Please select at least one variable")
                return

            # Get export options
            export_valor = self.checkBox_export_valor.isChecked()
            export_media = self.checkBox_export_media.isChecked()

            if not export_valor and not export_media:
                QtWidgets.QMessageBox.warning(self, "Export Error",
                    "Please select at least one export condition (_valor or _media)")
                return

            # Get paths
            output_path = self.output_path.text()
            name_project = self.name_project.text()
            scenario = self.comboBox_scenarios.currentText()
            simulation = self.comboBox_simulations.currentText()

            series_path = os.path.join(output_path, name_project, 'salidas', scenario, simulation, 'series')

            # Create output directory in temp/07_Resultados_Modelacion
            # temp folder is inside the project: output_path/name_project/temp
            temp_base = os.path.join(output_path, name_project, 'temp')
            temp_path = os.path.join(temp_base, '07_Resultados_Modelacion')
            os.makedirs(temp_path, exist_ok=True)

            self.textEdit_export_log.append(f"\n{'='*60}")
            self.textEdit_export_log.append(f"Starting export...")
            self.textEdit_export_log.append(f"Scenario: {scenario}")
            self.textEdit_export_log.append(f"Simulation: {simulation}")
            self.textEdit_export_log.append(f"Points: {len(selected_points)}")
            self.textEdit_export_log.append(f"Variables: {len(selected_variables)}")

            # Process each condition (_valor and/or _media)
            conditions = []
            if export_valor:
                conditions.append('valor')
            if export_media:
                conditions.append('media')

            # Initialize progress bar
            self.progressBar_proceso_2.setValue(0)
            # Calculate total operations: conditions × variables × points
            total_operations = len(conditions) * len(selected_variables) * len(selected_points)
            current_operation = 0

            for condition in conditions:
                self.textEdit_export_log.append(f"\nProcessing _{condition} files...")

                # Create Excel writer
                excel_filename = f'Results_{scenario}_{simulation}_{condition}.xlsx'
                excel_path = os.path.join(temp_path, excel_filename)

                with pd.ExcelWriter(excel_path, engine='openpyxl') as writer:

                    # Process each variable
                    for var in selected_variables:
                        # Create dataframe for this variable
                        var_data = {}

                        # Read data for each point
                        for point in selected_points:
                            series_file = os.path.join(series_path, f'{point}_{condition}.txt')

                            if not os.path.exists(series_file):
                                self.textEdit_export_log.append(f"⚠ File not found: {point}_{condition}.txt")
                                current_operation += 1
                                continue

                            # Read series file
                            df = ReadTimeSeries(series_file)

                            # Check if variable exists in this file
                            if var not in df.columns:
                                self.textEdit_export_log.append(f"⚠ Variable {var} not found in {point}")
                                current_operation += 1
                                continue

                            # Add this point's data for this variable
                            var_data[point] = df[var]

                            # Update progress bar
                            current_operation += 1
                            progress_percent = int((current_operation / total_operations) * 100)
                            self.progressBar_proceso_2.setValue(progress_percent)

                            # Process events to update UI
                            QtWidgets.QApplication.processEvents()

                        # Create DataFrame with all points for this variable
                        if var_data:
                            var_df = pd.DataFrame(var_data)

                            # Clean variable name for sheet name (Excel has 31 char limit)
                            sheet_name = var[:31].replace('/', '_').replace('\\', '_')

                            # Write to Excel sheet
                            var_df.to_excel(writer, sheet_name=sheet_name)

                            self.textEdit_export_log.append(f"  ✓ Exported variable: {var} ({len(var_data)} points)")

                self.textEdit_export_log.append(f"\n✓ Excel file created: {excel_filename}")

            # Set progress bar to 100%
            self.progressBar_proceso_2.setValue(100)

            self.textEdit_export_log.append(f"\n{'='*60}")
            self.textEdit_export_log.append(f"✓ EXPORT COMPLETED")
            self.textEdit_export_log.append(f"Output folder: {temp_path}")

            # Show success message
            QtWidgets.QMessageBox.information(self, "Export Complete",
                f"Results exported successfully!\n\n"
                f"Location: {temp_path}\n\n"
                f"Files created: {len(conditions)}")

        except Exception as e:
            # Reset progress bar on error
            self.progressBar_proceso_2.setValue(0)

            error_msg = f"Error exporting results: {str(e)}"
            self.textEdit_export_log.append(f"\n⚠ {error_msg}")
            QgsMessageLog.logMessage(error_msg, 'siga_step_05_Sim', Qgis.Critical)
            QtWidgets.QMessageBox.critical(self, "Export Error",
                f"Failed to export results:\n\n{str(e)}")

