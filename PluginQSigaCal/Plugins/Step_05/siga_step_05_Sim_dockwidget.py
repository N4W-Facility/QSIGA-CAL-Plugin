    def get_base_csv_path(self, subfolder=''):
        """
        Get the base CSV path based on user selection.

        Parameters:
        -----------
        subfolder : str
            Subfolder within base path (e.g., 'muestreador', 'calibracion')

        Returns:
        --------
        str
            Full path to the base CSV folder
        """
        if self.Check_Base_File.isChecked() and self.path_base_files.text():
            # Use external folder
            base_path = self.path_base_files.text()
            if subfolder:
                return os.path.join(base_path, subfolder)
            return base_path
        else:
            # Use internal archivos_csv folder from Step_01
            parent_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), os.pardir))
            subplugin1_dir = os.path.join(parent_dir, 'Step_01')
            base_path = os.path.join(subplugin1_dir, 'archivos_csv')
            if subfolder:
                return os.path.join(base_path, subfolder)
            return base_path

