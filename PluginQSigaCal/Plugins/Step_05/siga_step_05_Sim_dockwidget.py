    def create_calibration_file(self, output_dir, file_name):
        """
        Create a new calibration parameters file using base CSV files.

        Parameters:
        -----------
        output_dir : str
            Path to entradas/ directory (NOT calibracion/).
            The Ficheros class will create calibracion/ subfolder automatically.
        file_name : str
            Name of the calibration file (e.g., Parameters_Project.txt)
        """
        import json

        try:
            # Show processing message
            QtWidgets.QMessageBox.information(self, "Creating File",
                "Creating calibration parameters file.\nPlease wait...")

            # Load JSON dictionary
            path_json = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'common_functions')
            json_file_path = os.path.join(path_json, 'Diccionario_ficheros.json')

            with open(json_file_path) as json_file:
                diccionarios_ficheros_cal = json.load(json_file)

            # Create Ficheros instance
            # IMPORTANT: ruta_cuenca_SIGA should point to entradas/ (NOT entradas/calibracion/)
            # The crear_archivo_factores_calibracion method creates calibracion/ automatically
            ficheros_instance = Ficheros(
                ruta_cuenca_SIGA=output_dir + os.path.sep,
                archivo_fact_cal=file_name
            )

            # Get base CSV path
            path_files = self.get_base_csv_path('calibracion')

            # Create calibration file
            # This will create the file at: output_dir/calibracion/file_name
            ficheros_instance.crear_archivo_factores_calibracion(
                diccionarios_ficheros_cal['dic_factores_calibracion'],
                path_files
            )

            # Build final path for success message
            final_path = os.path.join(output_dir, 'calibracion', file_name)

            # Show success message
            QtWidgets.QMessageBox.information(self, "Success",
                f"Calibration file created successfully!\n\n"
                f"Location: {final_path}")

        except Exception as e:
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Failed to create calibration file:\n\n{str(e)}")


