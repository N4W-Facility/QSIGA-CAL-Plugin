    def load_transformation_csv(self):
        """Load transformation dictionary from CSV file."""
        csv_path, _ = QFileDialog.getOpenFileName(
            self,
            "Select Transformation Dictionary CSV",
            "",
            "CSV Files (*.csv);;All Files (*)"
        )

        if not csv_path:
            return

        try:
            # Read CSV file
            import csv
            transformation_dict = {}

            with open(csv_path, 'r', encoding='utf-8') as f:
                reader = csv.reader(f)
                header = next(reader, None)  # Skip header if present

                # Check if first row looks like a header
                if header and any(s.lower() in ['portfolio', 'value', 'nbs', 'action'] for s in header):
                    pass  # Header found, continue
                else:
                    # No header, process first row as data
                    if header:
                        try:
                            portfolio_val = int(header[0].strip())
                            nbs_action = int(header[1].strip())
                            transformation_dict[portfolio_val] = nbs_action
                        except (ValueError, IndexError):
                            pass

                # Read data rows
                for row in reader:
                    if len(row) >= 2:
                        try:
                            portfolio_val = int(row[0].strip())
                            nbs_action = int(row[1].strip())
                            transformation_dict[portfolio_val] = nbs_action
                        except ValueError:
                            continue

            if not transformation_dict:
                QtWidgets.QMessageBox.warning(self, "Empty CSV",
                    "No valid transformation mappings found in CSV file.")
                return

            # Calculate valor_fuera_poligono automatically
            max_action = max(transformation_dict.values())
            self.valor_fuera_poligono = max_action + 1

            # Store the dictionary
            self.dict_transformacion = transformation_dict

            # Store the path in lineEdit_path_dictionary
            self.lineEdit_path_dictionary.setText(csv_path)

            # Prepare detailed information for display
            csv_filename = os.path.basename(csv_path)

            # Build detailed message showing all mappings
            mappings_text = "Transformation Mappings:\n"
            for portfolio_val, nbs_action in sorted(transformation_dict.items()):
                action_name = {
                    1: "Conservation",
                    2: "Active Reforestation",
                    3: "Natural Regeneration",
                    4: "No Action"
                }.get(nbs_action, f"Action {nbs_action}")
                mappings_text += f"  Portfolio {portfolio_val} → NbS Action {nbs_action} ({action_name})\n"

            # Show detailed information window
            QtWidgets.QMessageBox.information(self, "Transformation Dictionary Loaded",
                f"✓ CSV File Loaded Successfully\n\n"
                f"File: {csv_filename}\n"
                f"Path: {csv_path}\n\n"
                f"{mappings_text}\n"
                f"Total mappings: {len(transformation_dict)}\n"
                f"Degradation value (outside polygon): {self.valor_fuera_poligono}")

            QgsMessageLog.logMessage(
                f"Transformation CSV loaded: {csv_path}, {len(transformation_dict)} mappings, "
                f"valor_fuera_poligono={self.valor_fuera_poligono}",
                'siga_step_05_Sim', Qgis.Info)

        except Exception as e:
            QtWidgets.QMessageBox.critical(self, "Error Loading CSV",
                f"Failed to load CSV file:\n{str(e)}")
            QgsMessageLog.logMessage(f"Error loading transformation CSV: {str(e)}",
                'siga_step_05_Sim', Qgis.Critical)

