    def copy_time_series_to_project(self, dynamic_output_path, scenario_type, output_path, name_project, scenario_name):
        """
        Copy time series files from Temp/06_dinamic_simulation/Outputs to entradas/series
        with proper naming: TimeSeries_TasRest_{scenario_name}.txt
        """
        try:
            # Define source folder (where series are generated)
            # Look for series files in subdirectories of dynamic_output_path
            series_files_found = []

            # Search patterns for series files based on scenario type
            if scenario_type == 'A':
                # Scenario A generates series_TasRest*.txt files
                pattern = 'series_TasRest*.txt'
            elif scenario_type == 'B':
                # Scenario B generates series_TasRestBaU.txt
                pattern = 'series_TasRestBaU.txt'
            elif scenario_type == 'C':
                # Scenario C generates multiple series_TasRest*.txt (one per budget)
                pattern = 'series_TasRest*.txt'
            elif scenario_type == 'D':
                # Scenario D generates series_TasRestBaU_Dinamico.txt
                pattern = 'series_TasRestBaU_Dinamico.txt'
            else:
                pattern = 'series_TasRest*.txt'

            # Find all series files in output directory
            import glob
            search_path = os.path.join(dynamic_output_path, '**', pattern)
            series_files_found = glob.glob(search_path, recursive=True)

            if not series_files_found:
                self.textEdit_log_gen.append(f"\n⚠ Warning: No time series files found matching pattern: {pattern}")
                QgsMessageLog.logMessage(
                    f"No time series files found in {dynamic_output_path} with pattern {pattern}",
                    'siga_step_05_Sim', Qgis.Warning
                )
                return

            # Define destination folder
            series_dest_folder = os.path.join(output_path, name_project, 'entradas', 'series')
            os.makedirs(series_dest_folder, exist_ok=True)

            # Copy and rename each series file
            for source_file in series_files_found:
                # Generate new filename
                if scenario_type == 'C':
                    # For Scenario C, preserve budget info in filename
                    original_filename = os.path.basename(source_file)
                    # Extract budget info from original name if present
                    if 'series_TasRest' in original_filename:
                        # e.g., series_TasRestPresup_75M.txt -> TimeSeries_TasRest_{scenario_name}_75M.txt
                        budget_part = original_filename.replace('series_TasRest', '').replace('.txt', '')
                        new_filename = f'TimeSeries_TasRest_{scenario_name}{budget_part}.txt'
                    else:
                        new_filename = f'TimeSeries_TasRest_{scenario_name}.txt'
                else:
                    # For other scenarios, use standard naming
                    new_filename = f'TimeSeries_TasRest_{scenario_name}.txt'

                dest_file = os.path.join(series_dest_folder, new_filename)

                # Copy the file
                shutil.copy2(source_file, dest_file)

                self.textEdit_log_gen.append(f"\n✓ Time series copied: {new_filename}")
                QgsMessageLog.logMessage(
                    f"Time series copied from {source_file} to {dest_file}",
                    'siga_step_05_Sim', Qgis.Info
                )

            self.textEdit_log_gen.append(f"\n✓ All time series exported to: {series_dest_folder}")

        except Exception as e:
            error_msg = f"Error copying time series: {str(e)}"
            self.textEdit_log_gen.append(f"\n⚠ {error_msg}")
            QgsMessageLog.logMessage(error_msg, 'siga_step_05_Sim', Qgis.Warning)

