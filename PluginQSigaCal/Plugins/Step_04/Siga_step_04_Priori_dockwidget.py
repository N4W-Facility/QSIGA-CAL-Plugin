    def create_portfolio_from_lulc(self):
        """
        Create NBS portfolio raster from original LULC raster using rules table.

        Process:
        1. Load original LULC raster
        2. Load rules table (LULC -> NBS mapping)
        3. Transform LULC codes to NBS codes based on table
        4. Set cells without matching rules to NoData
        5. Save portfolio raster
        """
        # Get paths
        lulc_path = self.lulc_original.text()
        table_path = self.Table_Rules.text()

        # Validate inputs
        if not lulc_path or not os.path.exists(lulc_path):
            QtWidgets.QMessageBox.warning(self, "Input Error",
                "Please select and load the original LULC raster first.")
            return

        if not table_path or not os.path.exists(table_path):
            QtWidgets.QMessageBox.warning(self, "Input Error",
                "Please select the rules table first.")
            return

        # Get project info
        name_project = self.name_project.text()
        output_path = self.output_path.text()

        try:
            # Show processing message
            QtWidgets.QMessageBox.information(self, "Processing",
                "Creating NBS portfolio from LULC.\n\nPlease wait...")

            # Load rules table
            if table_path.endswith('.csv'):
                rules_df = pd.read_csv(table_path)
            else:
                rules_df = pd.read_excel(table_path)

            # Verify required columns (case-insensitive)
            rules_df.columns = rules_df.columns.str.strip()
            column_map = {}
            for col in rules_df.columns:
                if col.upper() == 'LULC':
                    column_map['LULC'] = col
                elif col.upper() == 'NBS':
                    column_map['NBS'] = col

            if 'LULC' not in column_map or 'NBS' not in column_map:
                QtWidgets.QMessageBox.critical(self, "Error",
                    "Rules table must have 'LULC' and 'NBS' columns.\n\n"
                    "Found columns: " + ", ".join(rules_df.columns))
                return

            # Create LULC -> NBS mapping dictionary
            lulc_to_nbs = {}
            for index, row in rules_df.iterrows():
                lulc_code = int(row[column_map['LULC']])
                nbs_code = int(row[column_map['NBS']])
                lulc_to_nbs[lulc_code] = nbs_code

            print(f"LULC to NBS mapping: {lulc_to_nbs}")

            # Load LULC raster
            with rasterio.open(lulc_path) as src:
                lulc_array = src.read(1)
                profile = src.profile
                nodata_value = src.nodata if src.nodata is not None else -9999

            # Create portfolio array initialized with NoData
            portfolio_array = np.full_like(lulc_array, nodata_value, dtype=np.int16)

            # Apply transformation based on rules
            transform_count = 0
            for lulc_code, nbs_code in lulc_to_nbs.items():
                mask = (lulc_array == lulc_code)
                pixel_count = np.sum(mask)
                if pixel_count > 0:
                    portfolio_array[mask] = nbs_code
                    transform_count += pixel_count
                    print(f"Transformed {pixel_count} pixels: LULC {lulc_code} -> NBS {nbs_code}")

            # Count pixels without transformation
            nodata_count = np.sum(portfolio_array == nodata_value)

            # Update profile for output
            profile.update(dtype=rasterio.int16, nodata=nodata_value)

            # Save portfolio raster
            output_portfolio_path = os.path.join(output_path, name_project, 'temp', '05-Prioritization',
                                                  'Portfolio_NBS.tif')
            os.makedirs(os.path.dirname(output_portfolio_path), exist_ok=True)

            with rasterio.open(output_portfolio_path, 'w', **profile) as dst:
                dst.write(portfolio_array, 1)

            # Update portfolio path field (for use in create_new_lulc)
            self.Portafolio_Path.setText(output_portfolio_path)

            # Load to QGIS
            layer = QgsRasterLayer(output_portfolio_path, "Portfolio_NBS")
            if layer.isValid():
                QgsProject.instance().addMapLayer(layer)

            # Show success message
            QtWidgets.QMessageBox.information(self, "Success",
                f"NBS Portfolio created successfully!\n\n"
                f"Transformed pixels: {transform_count:,}\n"
                f"NoData pixels: {nodata_count:,}\n"
                f"Total pixels: {lulc_array.size:,}\n\n"
                f"Location: {output_portfolio_path}\n"
                f"Layer added to QGIS project.\n\n"
                f"The portfolio path has been automatically set for creating new LULC.")

        except Exception as e:
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Failed to create portfolio:\n\n{str(e)}")

