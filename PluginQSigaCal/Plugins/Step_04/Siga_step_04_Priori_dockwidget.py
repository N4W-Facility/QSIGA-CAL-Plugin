    def create_new_lulc(self):
        """
        Create new LULC raster combining base LULC, portfolio, and transition table.

        Process logic:
        1. Load base LULC (Step 1), portfolio raster, and reference LULC (Step 2)
        2. Where portfolio has value: Transform base LULC according to transition table
        3. Where portfolio is NoData: Keep values from reference LULC (Step 2)
        """
        try:
            # Get project configuration
            name_project = self.name_project.text()
            name_scenario_base = self.name_scenario.text()  # Step 1 base scenario
            name_scenario_initial = self.name_scenario_initial.text()  # Step 2 initial scenario
            name_scenario_nbs = self.name_scenario_Nbs.text()  # Step 3 NBS scenario
            output_path = self.output_path.text()

            # Get portfolio and transition table paths
            portfolio_path = self.Portafolio_Path.text()
            transition_table_path = self.Table_Path.text()

            # Validate required inputs
            if not all([name_project, name_scenario_base, name_scenario_initial,
                       name_scenario_nbs, output_path, portfolio_path, transition_table_path]):
                QMessageBox.warning(self, "Missing Information",
                                  "Please complete all required fields:\n"
                                  "- Project name and scenarios\n"
                                  "- Portfolio raster\n"
                                  "- Transition table")
                return

            QMessageBox.information(self, "Process Started",
                                  "Creating new LULC raster. Please wait until the process is finished.")

            # Define input paths
            # Base LULC from Step 1 base scenario
            base_lulc_path = os.path.join(output_path, name_project, 'entradas', 'mapas',
                                         name_scenario_base, 'CLC.tif')

            # Reference LULC from Step 2 initial scenario
            reference_lulc_path = os.path.join(output_path, name_project, 'entradas', 'mapas',
                                              name_scenario_initial, 'CLC.tif')

            # Output path for new LULC in Step 3 NBS scenario
            output_lulc_path = os.path.join(output_path, name_project, 'entradas', 'mapas',
                                           name_scenario_nbs, 'CLC.tif')

            # Verify all input files exist
            if not os.path.exists(base_lulc_path):
                QMessageBox.critical(self, "File Not Found",
                                   f"Base LULC not found:\n{base_lulc_path}\n"
                                   "Please complete Step 1 first.")
                return

            if not os.path.exists(reference_lulc_path):
                QMessageBox.critical(self, "File Not Found",
                                   f"Reference LULC not found:\n{reference_lulc_path}\n"
                                   "Please complete Step 2 first.")
                return

            if not os.path.exists(portfolio_path):
                QMessageBox.critical(self, "File Not Found",
                                   f"Portfolio not found:\n{portfolio_path}")
                return

            # Read transition table (use first sheet automatically, no specific sheet name)
            transition_table = pd.read_excel(transition_table_path, sheet_name=0)

            # Verify required columns (check for both 'SbN' and 'NBS')
            required_cols = ['lucode', 'lucode_end']
            nbs_col = 'NBS' if 'NBS' in transition_table.columns else 'SbN'

            if nbs_col not in transition_table.columns:
                QMessageBox.critical(self, "Invalid Table",
                                   "Transition table must have 'NBS' or 'SbN' column.")
                return

            for col in required_cols:
                if col not in transition_table.columns:
                    QMessageBox.critical(self, "Invalid Table",
                                       f"Transition table missing required column: {col}")
                    return

            # Create mapping dictionary: (lucode, NBS) → lucode_end
            transition_map = {}
            for _, row in transition_table.iterrows():
                key = (int(row['lucode']), int(row[nbs_col]))
                transition_map[key] = int(row['lucode_end'])

            print(f"Transition mapping created: {len(transition_map)} rules")

            # Read rasters
            with rasterio.open(base_lulc_path) as src_base:
                base_lulc = src_base.read(1)
                profile = src_base.profile
                nodata_base = src_base.nodata

            with rasterio.open(reference_lulc_path) as src_ref:
                reference_lulc = src_ref.read(1)
                nodata_ref = src_ref.nodata

            with rasterio.open(portfolio_path) as src_portfolio:
                portfolio = src_portfolio.read(1)
                nodata_portfolio = src_portfolio.nodata

            # Initialize output raster with reference LULC values
            # This ensures areas without portfolio intervention keep reference values
            output_lulc = np.copy(reference_lulc)

            # Create mask for areas with portfolio values (not NoData)
            if nodata_portfolio is not None:
                portfolio_mask = portfolio != nodata_portfolio
            else:
                # If no nodata value, assume NaN or negative values are nodata
                portfolio_mask = ~np.isnan(portfolio.astype(float))

            # Apply transformations where portfolio has values
            transformation_count = 0
            for (lucode, nbs_value), lucode_end in transition_map.items():
                # Find cells where:
                # 1. Portfolio has the NBS value
                # 2. Base LULC has the matching lucode
                mask = portfolio_mask & (portfolio == nbs_value) & (base_lulc == lucode)

                pixel_count = np.sum(mask)
                if pixel_count > 0:
                    output_lulc[mask] = lucode_end
                    transformation_count += pixel_count
                    print(f"Transformed {pixel_count} pixels: LULC {lucode} + NBS {nbs_value} → LULC {lucode_end}")

            # Update profile for output
            profile.update(dtype='int32', nodata=nodata_ref if nodata_ref is not None else -9999)

            # Ensure output directory exists
            os.makedirs(os.path.dirname(output_lulc_path), exist_ok=True)

            # Save output raster
            with rasterio.open(output_lulc_path, 'w', **profile) as dst:
                dst.write(output_lulc.astype(np.int32), 1)

            # Load into QGIS
            self.load_raster_layer(output_lulc_path, f"LULC_NBS_{name_scenario_nbs}")

            QMessageBox.information(self, "Process Complete",
                                  f"New LULC raster created successfully!\n\n"
                                  f"Transformed pixels: {transformation_count:,}\n"
                                  f"Output: {output_lulc_path}\n\n"
                                  f"Areas with portfolio: Transformed according to rules\n"
                                  f"Areas without portfolio: Reference scenario values")

        except Exception as e:
            QMessageBox.critical(self, "Error Creating LULC",
                               f"Error processing LULC transformation:\n{str(e)}\n\n"
                               f"Traceback:\n{traceback.format_exc()}")

