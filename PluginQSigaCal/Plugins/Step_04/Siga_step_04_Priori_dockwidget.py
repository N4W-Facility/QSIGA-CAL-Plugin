    def create_calibration_file(self, output_dir, file_name):
        """
        Create a new calibration parameters file using base CSV files.

        Parameters:
        -----------
        output_dir : str
            Path to entradas/ directory (NOT calibracion/).
            The Ficheros class will create calibracion/ subfolder automatically.
        file_name : str
            Name of the calibration file (e.g., Parameters_Project.txt)
        """
        import json

        try:
            # Show processing message
            QtWidgets.QMessageBox.information(self, "Creating File",
                "Creating calibration parameters file.\nPlease wait...")

            # Load JSON dictionary
            path_json = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'common_functions')
            json_file_path = os.path.join(path_json, 'Diccionario_ficheros.json')

            with open(json_file_path) as json_file:
                diccionarios_ficheros_cal = json.load(json_file)

            # Create Ficheros instance
            # IMPORTANT: ruta_cuenca_SIGA should point to entradas/ (NOT entradas/calibracion/)
            # The crear_archivo_factores_calibracion method creates calibracion/ automatically
            ficheros_instance = Ficheros(
                ruta_cuenca_SIGA=output_dir + os.path.sep,
                archivo_fact_cal=file_name
            )

            # Get base CSV path
            path_files = self.get_base_csv_path('calibracion')

            # Create calibration file
            # This will create the file at: output_dir/calibracion/file_name
            ficheros_instance.crear_archivo_factores_calibracion(
                diccionarios_ficheros_cal['dic_factores_calibracion'],
                path_files
            )

            # Build final path for success message
            final_path = os.path.join(output_dir, 'calibracion', file_name)

            # Show success message
            QtWidgets.QMessageBox.information(self, "Success",
                f"Calibration file created successfully!\n\n"
                f"File: {file_name}\n"
                f"Location: {final_path}\n\n"
                f"Base files used from: {path_files}")

        except FileNotFoundError as e:
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Base CSV files not found.\n\n"
                f"Please ensure base files exist in the selected folder.\n\n"
                f"Error: {str(e)}")
        except Exception as e:
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Failed to create calibration file.\n\n"
                f"Error: {str(e)}")


    def create_file_execution(self,escenario,file_Exe,start_date, end_date):
        # Obtener los valores de los campos de texto
        name_project = self.name_project.text()
        name_scenario = escenario
        Name_Basin = self.Name_Basin.text()
        ProjectPath = self.output_path.text()

        # Detect if this is Step 2 (Base) or Step 3 (NBS) based on scenario name
        name_scenario_nbs = self.name_scenario_Nbs.text()
        is_nbs_scenario = (name_scenario == name_scenario_nbs)

        # Use appropriate fields based on scenario type
        if is_nbs_scenario:
            # Step 3 NBS scenario
            user_input = self.name_calibration_file_nbs.text().strip()
            sampler_field = self.sampler_file_name_Nbs
        else:
            # Step 2 Base scenario
            user_input = self.name_calibration_file.text().strip()
            sampler_field = self.sampler_file_name

        # Build standard calibration filename: Parameters_{user_input}.txt
        # If user already entered the full format, extract just the user part
        if user_input.startswith('Parameters_') and user_input.endswith('.txt'):
            # Already in correct format, use as is
            calibration_file_name = user_input
        elif user_input.startswith('Parameters_'):
            # Has Parameters_ but missing .txt
            calibration_file_name = user_input + '.txt'
        elif user_input.endswith('.txt'):
            # Has .txt but missing Parameters_
            user_part = user_input.replace('.txt', '')
            calibration_file_name = f'Parameters_{user_part}.txt'
        else:
            # Just the user input, add both
            calibration_file_name = f'Parameters_{user_input}.txt'

        # Update the appropriate UI field to show the standard format
        if is_nbs_scenario:
            self.name_calibration_file_nbs.setText(calibration_file_name)
        else:
            self.name_calibration_file.setText(calibration_file_name)

        # Build sampler filename with standard format: Sampler_{user_input}.txt
        sampler_user_input = sampler_field.text().strip()

        # Get base scenario name from Step 1 (not the new scenario from Step 2)
        name_scenario_base = self.name_scenario.text()

        if not sampler_user_input:
            # If empty, use base scenario name as default (from Step 1)
            sampler_file_name = f'Sampler_{name_scenario_base}.txt'
        elif sampler_user_input.startswith('Sampler_') and sampler_user_input.endswith('.txt'):
            # Already in correct format, use as is
            sampler_file_name = sampler_user_input
        elif sampler_user_input.startswith('Sampler_'):
            # Has Sampler_ but missing .txt
            sampler_file_name = sampler_user_input + '.txt'
        elif sampler_user_input.endswith('.txt'):
            # Has .txt but missing Sampler_
            user_part = sampler_user_input.replace('.txt', '')
            sampler_file_name = f'Sampler_{user_part}.txt'
        else:
            # Just the user input, add both
            sampler_file_name = f'Sampler_{sampler_user_input}.txt'

        # Update the appropriate UI field to show the standard format
        if is_nbs_scenario:
            self.sampler_file_name_Nbs.setText(sampler_file_name)
        else:
            self.sampler_file_name.setText(sampler_file_name)

