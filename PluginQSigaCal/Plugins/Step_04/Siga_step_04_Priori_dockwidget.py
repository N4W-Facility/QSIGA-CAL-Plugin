    def process_spatial_prioritization(self):
        """
        Ejecuta el proceso de priorización espacial (Pasos 5-8).
        Requiere que se haya ejecutado previamente la extracción de inputs.
        """
        try:
            # Obtener parámetros del proyecto
            name_project = self.name_project.text()
            name_scenario = self.name_scenario_initial.text()
            Name_Basin = self.Name_Basin.text()
            new_escenario = self.name_scenario_Nbs.text()
            output_path = self.output_path.text()

            # Validar inputs
            if not all([name_project, name_scenario, Name_Basin, new_escenario, output_path]):
                QtWidgets.QMessageBox.warning(self, "Input Error",
                    "Please complete all required fields in previous steps.")
                return

            # Verificar que existe el archivo de cuenca
            path_result = os.path.join(output_path, name_project, 'temp', '05-Prioritization')
            cuenca_excel_path = os.path.join(path_result, '01_Result', f'cuencaDk_{Name_Basin}.xlsx')

            if not os.path.exists(cuenca_excel_path):
                QtWidgets.QMessageBox.critical(self, "Error",
                    f"Basin file not found:\n{cuenca_excel_path}\n\n"
                    f"Please run 'Run_Extrat_Inputs' process first.")
                return

            # Verificar shapefile de polígonos
            rutaPoligonos = self.Path_Poligonos.text()
            if not rutaPoligonos or not os.path.exists(rutaPoligonos):
                QtWidgets.QMessageBox.critical(self, "Error",
                    "Please select a valid polygon shapefile.")
                return

            # Obtener pesos de ponderación de la UI
            try:
                fcPonderacion = {
                    'flujoBase':  float(self.w_bf.text()) if self.w_bf.text() else 1.0,
                    'sedimentos': float(self.w_s.text()) if self.w_s.text() else 1.0,
                    'EC':         float(self.w_ecoli.text()) if self.w_ecoli.text() else 0.2,
                    'N':          float(self.w_n.text()) if self.w_n.text() else 0.6,
                    'P':          float(self.w_P.text()) if self.w_P.text() else 0.7
                }
            except ValueError:
                QtWidgets.QMessageBox.critical(self, "Error",
                    "Weighting factors must be valid numbers.")
                return

            # Porcentajes de intervención fijos
            percIntervencion = [20, 40, 80]

            # Rutas a coberturas LULC
            rutaCoberturasLineaBase = os.path.join(output_path, name_project, 'entradas', 'mapas',
                                                    name_scenario, 'CLC.tif')
            rutaCoberturasRestauracion = os.path.join(output_path, name_project, 'entradas', 'mapas',
                                                       new_escenario, 'CLC.tif')

            # Verificar que existan las coberturas
            if not os.path.exists(rutaCoberturasLineaBase):
                QtWidgets.QMessageBox.critical(self, "Error",
                    f"Base land cover not found:\n{rutaCoberturasLineaBase}")
                return

            if not os.path.exists(rutaCoberturasRestauracion):
                QtWidgets.QMessageBox.critical(self, "Error",
                    f"NBS land cover not found:\n{rutaCoberturasRestauracion}")
                return

            # Leer cuenca desde Excel
            import pandas as pd
            cuenca = pd.read_excel(cuenca_excel_path)

            # Mensaje de inicio
            QtWidgets.QMessageBox.information(self, "Processing",
                "Starting spatial prioritization process (Steps 5-8).\n"
                "This process may take several minutes.\nPlease wait...")

            # Inicializar barra de progreso
            self.progressBar_proceso_2.setValue(0)

            # Importar matplotlib si no está importado
            import matplotlib.pyplot as plt

            # Ejecutar proceso de priorización espacial (Pasos 5-8)
            # Aquí llamaremos a las funciones de Fuctions_Priority.py

            # ========== PASO 5: Agregación espacial por polígonos ==========
            self.progressBar_proceso_2.setValue(10)
            QtWidgets.QApplication.processEvents()

            import geopandas as gpd

            # Leer shapefile de polígonos
            poligonos = gpd.read_file(rutaPoligonos)

            # Obtener CRS del proyecto desde el Watershed.shp
            watershed_path = os.path.join(output_path, name_project, 'temp', '01_Delimitacion_Cuenca', 'Watershed.shp')

            if not os.path.exists(watershed_path):
                QtWidgets.QMessageBox.critical(self, "Error",
                    f"Project Watershed.shp file not found:\n{watershed_path}\n\n"
                    f"This file is needed to determine the project coordinate system.")
                return

            # Leer CRS del proyecto
            watershed = gpd.read_file(watershed_path)
            project_crs = watershed.crs

            if project_crs is None:
                QtWidgets.QMessageBox.critical(self, "Error",
                    f"Watershed.shp file has no coordinate system defined.\n"
                    f"Please verify the project configuration.")
                return

            # Verificar y convertir CRS del shapefile de polígonos
            if poligonos.crs is None:
                QtWidgets.QMessageBox.critical(self, "CRS Error",
                    f"Polygon shapefile has no coordinate system defined.\n\n"
                    f"Please assign a coordinate system to the shapefile before continuing.\n"
                    f"Project is configured with: {project_crs}")
                return
            else:
                # Convertir al CRS del proyecto
                poligonos = poligonos.to_crs(project_crs)

            poligonos['AreaTotal'] = poligonos.area

            # Obtener nombre del campo ID (normalmente el primer campo)
            nombreID = poligonos.columns[0]

            poligonos = poligonos.dropna(subset=[nombreID], axis=0)
            poligonos = poligonos.dissolve(by=nombreID, aggfunc='first')
            poligonos = poligonos.reset_index()

            self.progressBar_proceso_2.setValue(20)
            QtWidgets.QApplication.processEvents()

            # Rastreo de celdas con cambio
            from ..common_functions.Raster import Raster

            coberturasLineaBase = Raster(rutaCoberturasLineaBase)
            coberturasRestauracion = Raster(rutaCoberturasRestauracion)

            cuenca['cobLineaBase'] = coberturasLineaBase.getValueByCoordinates(cuenca.X, cuenca.Y)
            cuenca['cobRestauracion'] = coberturasRestauracion.getValueByCoordinates(cuenca.X, cuenca.Y)

            cuenca['cambio'] = 0
            cuenca.loc[cuenca['cobLineaBase'] != cuenca['cobRestauracion'], 'cambio'] = 1

            self.progressBar_proceso_2.setValue(30)
            QtWidgets.QApplication.processEvents()

            # Inferencia de tamaños de celda
            clsz = []
            clsz.append(np.nanmean(np.sort(cuenca.X.unique())[1:] - np.sort(cuenca.X.unique())[:-1]))
            clsz.append(np.nanmean(np.sort(cuenca.Y.unique())[1:] - np.sort(cuenca.Y.unique())[:-1]))
            clsz = tuple(clsz)
            dx = clsz[0] / 2.
            dy = clsz[1] / 2.

            # Crear GeoDataFrame con celdas con cambio
            cuencaCambio = cuenca.loc[cuenca['cambio'] == 1, :].copy()
            geometry = cuencaCambio.apply(lambda x: Priority.getGeometry(x, dx, dy), axis=1)
            cuencaCambio = gpd.GeoDataFrame(cuencaCambio, geometry=geometry, crs=project_crs)

            # Intersección
            poligonosInterCuenca = poligonos.overlay(cuencaCambio, how='intersection')

            self.progressBar_proceso_2.setValue(40)
            QtWidgets.QApplication.processEvents()

            # Cálculo de áreas y ponderación
            areaCelda = cuencaCambio.area.mean()
            poligonosInterCuenca['Area_m2'] = poligonosInterCuenca.area
            poligonosInterCuenca['PondArea'] = poligonosInterCuenca['Area_m2'] / areaCelda

            # Agregación de área con cambio
            areaConCambio = poligonosInterCuenca.groupby(nombreID)['Area_m2'].agg('sum')
            poligonos = poligonos.join(areaConCambio, on=nombreID, how='left')
            poligonos = poligonos.rename(columns={'Area_m2': 'AreaCambio'})
            poligonos['PercCambio'] = poligonos['AreaCambio'] / poligonos['AreaTotal']

            # ========== PASO 6: Cálculo de índice de priorización ==========
            self.progressBar_proceso_2.setValue(50)
            QtWidgets.QApplication.processEvents()

            columnasDk = [f'Dk_{key}' for key in fcPonderacion.keys()]

            for Dk in columnasDk:
                poligonosInterCuenca[Dk] = poligonosInterCuenca[Dk] * poligonosInterCuenca['PondArea']
                poligonosInterCuenca[f'{Dk}_tru'] = Priority.transformacionRangoUnitario(poligonosInterCuenca[Dk], minimo=0)

            # Dk total
            columnasDkTRU = [f'Dk_{key}_tru' for key in fcPonderacion.keys()]
            poligonosInterCuenca['Dk_total'] = 0.

            for DkTRU in columnasDkTRU:
                poligonosInterCuenca['Dk_total'] = poligonosInterCuenca['Dk_total'] + \
                    poligonosInterCuenca[DkTRU] * fcPonderacion[DkTRU.split('_')[1]]

            # Agrupación por polígonos
            columnasAnalisis = columnasDk + ['Dk_total', 'Area_m2']
            agrupacionPoligonalDk = poligonosInterCuenca.groupby(nombreID)[columnasAnalisis].agg('sum')
            agrupacionPoligonalDk = agrupacionPoligonalDk.reset_index()

            # Ordenación y área acumulada
            agrupacionPoligonalDk = agrupacionPoligonalDk.sort_values(by='Dk_total', ascending=False)
            areaTotalIntervenible = agrupacionPoligonalDk['Area_m2'].sum()
            agrupacionPoligonalDk['AreaAcu_m2'] = agrupacionPoligonalDk['Area_m2'].cumsum()
            agrupacionPoligonalDk['AreaAcuPer'] = (agrupacionPoligonalDk['AreaAcu_m2'] / areaTotalIntervenible) * 100.

            # Transformación de rango unitario y acumulados
            for Dk in columnasDk + ['Dk_total']:
                agrupacionPoligonalDk[f'{Dk}_tru'] = Priority.transformacionRangoUnitario(agrupacionPoligonalDk[Dk], minimo=0)
                agrupacionPoligonalDk[f'{Dk}_tru_acum'] = \
                    Priority.transformacionRangoUnitario(agrupacionPoligonalDk[f'{Dk}_tru'].cumsum())

            self.progressBar_proceso_2.setValue(60)
            QtWidgets.QApplication.processEvents()

            # ========== PASO 7: Generar visualizaciones ==========
            os.makedirs(os.path.join(path_result, '02_Prioritization'), exist_ok=True)

            fig, ax = plt.subplots(figsize=(10, 5))
            ax.set_title(Name_Basin.capitalize(), fontdict={'fontname': 'Calibri', 'fontweight': 'bold',
                                                             'fontsize': '20', 'va': 'bottom', 'color': 'darkslategrey'})
            ax.set_xlabel('Porcentaje acumulado de área intervenida',
                           fontdict={'fontname': 'Calibri', 'fontsize': '18', 'color': 'darkslategrey'})
            ax.set_ylabel('Entrega (Dk) en rango unitario',
                           fontdict={'fontname': 'Calibri', 'fontsize': '18', 'color': 'darkslategrey'})

            plt.xticks(fontname='Calibri', color='darkslategrey', size=14)
            plt.yticks(fontname='Calibri', color='darkslategrey', size=14)

            for col in columnasDkTRU + ['Dk_total_tru']:
                agrupacionPoligonalDk.plot.line(x='AreaAcuPer', y=f'{col}_acum', ax=ax, label=col.replace('_tru', ''))

            ax.grid(color='whitesmoke')
            legend = ax.legend(prop={'family': 'Calibri', 'size': '10'}, loc=4, framealpha=0.5)
            for text in legend.get_texts():
                text.set_color("darkslategrey")

            plt.savefig(os.path.join(path_result, '02_Prioritization', f'Dk_vs_AreaAcumPer_{Name_Basin}.png'), dpi=300)
            plt.close()

            # Exportar entregas
            entregas_path = os.path.join(path_result, '02_Prioritization', f'entregas_{Name_Basin}.xlsx')
            agrupacionPoligonalDk[['AreaAcu_m2', 'AreaAcuPer'] + [f'{col}_acum' for col in columnasDkTRU] + ['Dk_total_tru_acum']].to_excel(
                entregas_path, index=False)

            self.progressBar_proceso_2.setValue(80)
            QtWidgets.QApplication.processEvents()

            # ========== PASO 8: Generar escenarios de intervención ==========
            # Asignar prioridad
            agrupacionPoligonalDk['prioriz'] = np.arange(len(agrupacionPoligonalDk['Dk_total_tru_acum'])) + 1

            # Asignar escenarios de intervención
            for perc in percIntervencion:
                nameCol = f'escen{perc:03}'
                agrupacionPoligonalDk[nameCol] = 0
                agrupacionPoligonalDk.loc[agrupacionPoligonalDk['AreaAcuPer'] <= perc, nameCol] = 1

            # Adecuar índices
            poligonosInterCuenca.index = poligonosInterCuenca[nombreID]
            poligonosInterCuenca.index.name = None
            agrupacionPoligonalDk.index = agrupacionPoligonalDk[nombreID]
            agrupacionPoligonalDk.index.name = None

            # Join del DataFrame de agrupación sobre polígonos intersectados
            poligonosInterCuenca = poligonosInterCuenca.join(agrupacionPoligonalDk, how='left', lsuffix='_polig', rsuffix='_agrup')
            poligonosInterCuenca = poligonosInterCuenca.reset_index()

            # Asignar escenarios a celdas
            umbral = 0.5
            for perc in percIntervencion:
                nameCol = f'escen{perc:03}'
                cuenca[nameCol] = 0
                poligonosInterCuencaFilt = poligonosInterCuenca.loc[poligonosInterCuenca[nameCol] == 1, ['idCelda', 'PondArea']]
                poligonosInterCuencaFilt = poligonosInterCuencaFilt.groupby('idCelda').agg('sum')
                indicesCeldas = poligonosInterCuencaFilt[poligonosInterCuencaFilt['PondArea'] >= umbral].index
                cuenca.loc[indicesCeldas, nameCol] = 1

            # Generar coberturas de intervención
            import copy
            from ..common_functions.Raster import Raster

            cuencaCoberturas = copy.deepcopy(cuenca[['idCelda', 'tipo', 'X', 'Y', 'cobLineaBase', 'cobRestauracion'] + \
                                                    [f'escen{perc:03}' for perc in percIntervencion]])

            for perc in percIntervencion:
                # Asignar cobertura inicial por defecto
                cuencaCoberturas[f'cob{perc:03}'] = cuencaCoberturas['cobLineaBase']
                # Asignar cobertura de restauración a celdas a intervenir
                cuencaCoberturas.loc[cuencaCoberturas[f'escen{perc:03}'] == 1, f'cob{perc:03}'] = \
                    cuencaCoberturas['cobRestauracion']

                # Almacenamiento de cobertura en objeto Raster
                sourceDict = {
                    'x': cuencaCoberturas['X'],
                    'y': cuencaCoberturas['Y'],
                    'values': cuencaCoberturas[f'cob{perc:03}']
                }
                cobertura = Raster(source=sourceDict)
                cobertura.exportRaster(os.path.join(path_result, '02_Prioritization', f'CLC_{Name_Basin}_escen{perc:03}.tif'))
                cobertura.exportRaster(os.path.join(path_result, '02_Prioritization', f'CLC_{Name_Basin}_escen{perc:03}.sgabr'))

                # Comentado: lookup_idQ no está disponible por el momento
                # if lookup_idQ is not None:
                #     idQ = cobertura.lookupRaster(lookup=lookup_idQ)
                #     idQ.exportRaster(os.path.join(path_result, '02_Prioritization', f'idQ_{Name_Basin}_escen{perc:03}.tif'))
                #     idQ.exportRaster(os.path.join(path_result, '02_Prioritization', f'idQ_{Name_Basin}_escen{perc:03}.sgabr'))

            # Join con polígonos
            poligonos.index = poligonos[nombreID]
            poligonos.index.name = None
            poligonos = poligonos.join(agrupacionPoligonalDk, how='left', lsuffix='', rsuffix='_agrup')

            # Exportar shapefile
            shp_path = os.path.join(path_result, '02_Prioritization', f'poligonos_{Name_Basin}.shp')
            poligonos.to_file(filename=shp_path, driver="ESRI Shapefile")

            # Exportar Excel
            excel_path_poligonos = os.path.join(path_result, '02_Prioritization', f'poligonos_{Name_Basin}.xlsx')
            poligonos.to_excel(excel_path_poligonos)

            # Exportar cuenca con cambios
            cuenca_shp_path = os.path.join(path_result, '02_Prioritization', f'cuencaCambio_{Name_Basin}.shp')
            cuencaCambio.to_file(filename=cuenca_shp_path, driver="ESRI Shapefile")

            # Progress: 100%
            self.progressBar_proceso_2.setValue(100)
            QtWidgets.QApplication.processEvents()

            # Success message
            QtWidgets.QMessageBox.information(self, "Process Complete",
                f"Spatial prioritization process completed successfully!\n\n"
                f"Results saved in:\n{path_result}/02_Prioritization/\n\n"
                f"Generated files:\n"
                f"- Chart: Dk_vs_AreaAcumPer_{Name_Basin}.png\n"
                f"- Excel deliveries: entregas_{Name_Basin}.xlsx\n"
                f"- Polygon shapefile: poligonos_{Name_Basin}.shp\n"
                f"- {len(percIntervencion)} coverage scenarios (20%, 40%, 80%)\n\n"
                f"Applied weights:\n"
                f"Base Flow: {fcPonderacion['flujoBase']}\n"
                f"Sediments: {fcPonderacion['sedimentos']}\n"
                f"E. coli: {fcPonderacion['EC']}\n"
                f"Nitrogen: {fcPonderacion['N']}\n"
                f"Phosphorus: {fcPonderacion['P']}")

        except Exception as e:
            # Reset progress bar on error
            self.progressBar_proceso_2.setValue(0)
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Error during spatial prioritization process:\n\n{str(e)}\n\n"
                f"Traceback:\n{traceback.format_exc()}")

