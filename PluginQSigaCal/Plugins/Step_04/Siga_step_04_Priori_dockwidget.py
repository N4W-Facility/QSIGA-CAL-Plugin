    def copy_and_check_scenario(self):
        """
        Copy maps from base scenario to new scenario and verify all files.
        Note: CLC.tif is NOT copied - it must be loaded separately using Button_Load_LULC
        to ensure proper resolution and category validation.
        """
        # Get values from text fields
        name_project = self.name_project.text()
        name_scenario_base = self.name_scenario.text()  # Base scenario from Step 1
        name_scenario_new = self.name_scenario_initial.text()  # New scenario name
        output_path = self.output_path.text()

        # Validate inputs
        if not name_scenario_new:
            QtWidgets.QMessageBox.warning(self, "Input Error", "Please enter a name for the new scenario.")
            return

        # Source and destination directories
        directorio_origen = os.path.join(output_path, name_project, 'entradas', 'mapas', name_scenario_base)
        directorio_destino = os.path.join(output_path, name_project, 'entradas', 'mapas', name_scenario_new)

        # Files to copy (CLC.tif excluded - will be loaded separately)
        archivos_esperados = [
            'arcillas.tif', 'arenas.tif', 'idCiclo.tif', 'idQ.tif',
            'ki.tif', 'limos.tif', 'MO.tif', 'P_usle.tif', 'paramo.tif',
            'prioridad.tif', 'Q5a.tif', 'transicion.tif', 'Zs.tif', 'K_usle.tif'
        ]

        # Check if source directory exists
        if not os.path.isdir(directorio_origen):
            QtWidgets.QMessageBox.warning(self, "Error",
                f"Source directory does not exist:\n{directorio_origen}\n\nPlease verify the base scenario name.")
            return

        # Create destination directory if it doesn't exist
        os.makedirs(directorio_destino, exist_ok=True)

        # Copy files from source to destination
        archivos_copiados = []
        archivos_faltantes = []

        for archivo in archivos_esperados:
            ruta_origen = os.path.join(directorio_origen, archivo)
            ruta_destino = os.path.join(directorio_destino, archivo)

            try:
                if os.path.exists(ruta_origen):
                    shutil.copy2(ruta_origen, ruta_destino)
                    archivos_copiados.append(archivo)
                else:
                    archivos_faltantes.append(archivo)
            except Exception as e:
                QtWidgets.QMessageBox.warning(self, "Error",
                    f"Error copying file {archivo}:\n{str(e)}")
                return

        # Show results
        if not archivos_faltantes:
            QtWidgets.QMessageBox.information(self, "Success",
                f"New scenario '{name_scenario_new}' created successfully.\n\n"
                f"All {len(archivos_copiados)} files were copied from base scenario '{name_scenario_base}'.\n\n"
                f"IMPORTANT: CLC.tif was NOT copied.\n"
                f"Please use 'Button_Load_LULC' to load the land cover file.\n"
                f"This ensures proper resolution adjustment and category validation.")
        else:
            archivos_faltantes_str = ', '.join(archivos_faltantes)
            QtWidgets.QMessageBox.warning(self, "Partial Success",
                f"Scenario created but some files are missing in the base scenario:\n\n{archivos_faltantes_str}\n\n"
                f"Copied files: {len(archivos_copiados)}/{len(archivos_esperados)}\n\n"
                f"IMPORTANT: CLC.tif was NOT copied.\n"
                f"Please use 'Button_Load_LULC' to load the land cover file.")

    def load_and_resample_lulc(self):
        """
        Load a new LULC (Land Use Land Cover) raster and resample it to match existing rasters.
        Replaces categories not in calibration file with code 231 (Pastures).
        Replaces the CLC.tif in the new scenario folder.
        """
        from ..common_functions.Functions_Raster import resample_to_match
        from osgeo import gdal

        # Get values
        name_project = self.name_project.text()
        name_scenario_new = self.name_scenario_initial.text()
        output_path = self.output_path.text()

        if not name_scenario_new:
            QtWidgets.QMessageBox.warning(self, "Input Error",
                "Please create a new scenario first (Step 2).")
            return

        # Select LULC raster file
        raster_path, _ = QFileDialog.getOpenFileName(self, "Select LULC raster file", "",
                                                      "TIF Files (*.tif);;All Files (*)")
        if not raster_path:
            return

        # Update path display
        self.Path_Raster_LULC.setText(raster_path)

        # Define output paths
        scenario_maps_dir = os.path.join(output_path, name_project, 'entradas', 'mapas', name_scenario_new)
        modified_raster_path = os.path.join(scenario_maps_dir, 'CLC_modified.tif')
        output_clc_path = os.path.join(scenario_maps_dir, 'CLC.tif')

        # Find a reference raster (use any existing raster in the scenario folder)
        reference_raster = None
        for file in os.listdir(scenario_maps_dir):
            if file.endswith('.tif') and file != 'CLC.tif':
                reference_raster = os.path.join(scenario_maps_dir, file)
                break

        if not reference_raster:
            QtWidgets.QMessageBox.warning(self, "Error",
                "No reference raster found in scenario folder.\n\n"
                "Please ensure the scenario has been created with base maps.")
            return

        try:
            # Show processing message
            QtWidgets.QMessageBox.information(self, "Processing",
                "Processing LULC raster:\n"
                "1. Replacing categories not in calibration file\n"
                "2. Resampling to match scenario resolution\n\n"
                "Please wait...")

            # Step 1: Replace values not in calibration file
            # Categories that don't exist in calibration file are replaced with 231 (Pastures)
            raster = gdal.Open(raster_path, gdal.GA_ReadOnly)
            band = raster.GetRasterBand(1)
            arr = band.ReadAsArray()

            # Values to replace (categories not in calibration file)
            # These include artificial surfaces (4xx) and water bodies (5xx) that aren't defined
            values_to_replace = [411, 412, 413, 421, 422, 423, 511, 512, 513, 514, 521, 523, 0]

            # Count replaced values for user info
            replaced_count = 0
            for value in values_to_replace:
                count = np.sum(arr == value)
                if count > 0:
                    arr[arr == value] = 231  # Replace with Pastures (231)
                    replaced_count += count
                    print(f"Replaced {count} pixels with value {value} -> 231 (Pastures)")

            # Save modified array to temporary file
            driver = gdal.GetDriverByName('GTiff')
            temp_raster = driver.Create(modified_raster_path, raster.RasterXSize, raster.RasterYSize,
                                       1, gdal.GDT_Int16)
            temp_raster.SetGeoTransform(raster.GetGeoTransform())
            temp_raster.SetProjection(raster.GetProjection())
            temp_band = temp_raster.GetRasterBand(1)
            temp_band.WriteArray(arr)
            temp_band.FlushCache()

            # Release rasters from memory
            raster = None
            temp_raster = None

            # Step 2: Resample to match reference raster
            resample_to_match(modified_raster_path, reference_raster, output_clc_path, integer_output=True)

            # Clean up temporary file
            if os.path.exists(modified_raster_path):
                os.remove(modified_raster_path)

            # Load to QGIS
            layer = QgsRasterLayer(output_clc_path, f"CLC_{name_scenario_new}")
            if layer.isValid():
                QgsProject.instance().addMapLayer(layer)

            QtWidgets.QMessageBox.information(self, "Success",
                f"LULC raster processed successfully!\n\n"
                f"Replaced {replaced_count} pixels with categories not in calibration file\n"
                f"(Replaced with code 231 - Pastures)\n\n"
                f"Location: {output_clc_path}\n"
                f"Layer added to QGIS project.")

        except Exception as e:
            QtWidgets.QMessageBox.critical(self, "Error",
                f"Failed to process LULC raster:\n\n{str(e)}")

