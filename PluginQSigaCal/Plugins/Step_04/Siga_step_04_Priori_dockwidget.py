    def rename_simulation_folder(self, ProjectPath, name_project, name_scenario):
        """
        Rename the date-based simulation folder to scenario name.
        Converts: salidas/scenario_name/YYYYMMDD_HHMMSS/
        To: salidas/scenario_name/scenario_name/

        Parameters:
        -----------
        ProjectPath : str
            Path to project root
        name_project : str
            Name of the project
        name_scenario : str
            Name of the scenario
        """
        try:
            scenario_dir = os.path.join(ProjectPath, name_project, 'salidas', name_scenario)

            if not os.path.exists(scenario_dir):
                print(f"Scenario directory not found: {scenario_dir}")
                return

            # Look for date-based folders (format: YYYYMMDD_HHMMSS)
            date_folders = []
            for item in os.listdir(scenario_dir):
                item_path = os.path.join(scenario_dir, item)
                if os.path.isdir(item_path):
                    # Check if it's a date-based folder (starts with digits)
                    if item[0].isdigit() and '_' in item:
                        date_folders.append((item_path, os.path.getmtime(item_path)))

            if not date_folders:
                print(f"No date-based folders found in: {scenario_dir}")
                return

            # Get the most recent date-based folder
            date_folders.sort(key=lambda x: x[1], reverse=True)
            latest_folder = date_folders[0][0]

            # Target folder path
            target_folder = os.path.join(scenario_dir, name_scenario)

            # If target already exists, remove it first
            if os.path.exists(target_folder):
                print(f"Removing existing folder: {target_folder}")
                shutil.rmtree(target_folder)

            # Rename date-based folder to scenario name
            print(f"Renaming: {latest_folder} -> {target_folder}")
            os.rename(latest_folder, target_folder)
            print(f"✓ Simulation folder renamed successfully to: {name_scenario}")

        except Exception as e:
            print(f"Warning: Could not rename simulation folder: {str(e)}")
            # Don't raise exception - this is not critical for simulation success

