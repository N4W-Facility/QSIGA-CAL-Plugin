    def max_sum_in_windows(self,series, window_size):
        max_sum = -np.inf
        for i in range(len(series) - window_size + 1):
            current_sum = series[i:i + window_size].sum()
            if current_sum > max_sum:
                max_sum = current_sum
        return max_sum

