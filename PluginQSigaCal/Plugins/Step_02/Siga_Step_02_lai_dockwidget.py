    def Series_X_Y(self, catalogo, df, var):
        """
        Merge station catalog with time series data.

        Args:
            catalogo: GeoDataFrame with station metadata (indexed by 'codigo')
            df: DataFrame with time series data (Date as index, station names as columns)
            var: Variable type ('Tamax', 'Tamin', or 'PV')

        Returns:
            DataFrame with station coordinates and time series data
        """
        if var == 'Tamax' or var == 'Tamin':
            cols_select = ['y', 'x', 'z']
        else:
            cols_select = ['y', 'x']

        # Transpose: rows become station names, columns become dates
        data = df.loc[df.index[0]:].T
        columnas = data.columns
        str_list = [d.strftime('%Y-%m-%d') for d in columnas]

        # Keep station codes as strings (they can be numeric or alphanumeric)
        # data.index is now the station names from Excel columns
        data.columns = str_list
        data.index.name = 'codigo'

        # Get station coordinates from catalog
        df_new = catalogo[cols_select].copy()

        # Merge using string-based index (no conversion to int)
        # This works for both numeric codes (e.g., "12345") and alphanumeric (e.g., "M0045", "Chanlud_M")
        df_new = df_new.merge(data, left_index=True, right_index=True, how='inner')

        if len(df_new) == 0:
            # No matches found - provide helpful error message
            catalog_codes = set(catalogo.index.astype(str))
            excel_codes = set(data.index.astype(str))

            raise ValueError(
                f"No matching station codes found between shapefile and Excel.\n\n" +
                f"Shapefile codes (first 10): {list(catalog_codes)[:10]}\n\n" +
                f"Excel columns (first 10): {list(excel_codes)[:10]}\n\n" +
                f"Make sure the station codes in the shapefile 'Codigo' column\n" +
                f"match the column names in the Excel file.\n\n" +
                f"Common in both: {catalog_codes & excel_codes if catalog_codes & excel_codes else 'None'}"
            )

        return df_new

    def elevaciones(self, path_dem, path_lulc):
        """
        Calculate mean elevation for each land cover class.
        Properly handles NoData values in both LULC and DEM rasters.
        """
        # Read LULC raster
        with rasterio.open(path_lulc) as src:
            coverage_data = src.read(1)
            coverage_meta = src.meta
            coverage_nodata = coverage_meta.get('nodata')

        # Read DEM raster
        with rasterio.open(path_dem) as src:
            Dem_data = src.read(1)
            Dem_meta = src.meta
            dem_nodata = Dem_meta.get('nodata')

        # Verify dimensions match
        if coverage_data.shape != Dem_data.shape:
            QgsMessageLog.logMessage(
                f"Dimension mismatch: LULC {coverage_data.shape} vs DEM {Dem_data.shape}",
                "QSIGA_CAL",
                Qgis.Warning
            )
            raise ValueError(
                f"Dimension mismatch!\n" +
                f"  LULC shape: {coverage_data.shape}\n" +
                f"  DEM shape: {Dem_data.shape}\n" +
                f"Rasters must have the same dimensions."
            )

        # Get unique land cover codes (excluding NoData)
        coverage_codes = np.unique(coverage_data)
        if coverage_nodata is not None:
            coverage_codes = coverage_codes[coverage_codes != coverage_nodata]

        # Calculate mean elevation for each land cover class
        elv_series = {code: [] for code in coverage_codes}
        nodata_count = 0

        for code in coverage_codes:
            # Create mask for this land cover class
            lulc_mask = coverage_data == code

            if not np.any(lulc_mask):
                # No pixels with this land cover code
                elv_series[code].append(np.nan)
                continue

            # Get DEM values for this land cover class
            dem_values = Dem_data[lulc_mask]

            # Filter out NoData values from DEM
            if dem_nodata is not None:
                # Create valid data mask (exclude NoData)
                valid_mask = dem_values != dem_nodata
                dem_values_valid = dem_values[valid_mask]
            else:
                dem_values_valid = dem_values

            # Also filter out NaN values (in case DEM has them)
            dem_values_valid = dem_values_valid[~np.isnan(dem_values_valid)]

            # Calculate mean elevation
            if len(dem_values_valid) > 0:
                elv_mean = np.mean(dem_values_valid)
                elv_series[code].append(elv_mean)

                # Count NoData pixels for logging
                total_pixels = len(dem_values)
                valid_pixels = len(dem_values_valid)
                nodata_pixels = total_pixels - valid_pixels

                if nodata_pixels > 0:
                    nodata_pct = (nodata_pixels / total_pixels) * 100
                    if nodata_pct > 10:  # Log if > 10% NoData
                        nodata_count += 1
            else:
                # All DEM values are NoData for this land cover class
                elv_series[code].append(np.nan)
                nodata_count += 1

        # Log warnings about NoData
        if nodata_count > 0:
            QgsMessageLog.logMessage(
                f"{nodata_count} land cover classes have significant NoData in DEM",
                "QSIGA_CAL",
                Qgis.Warning
            )

        df_elv = pd.DataFrame(elv_series)

        # Check for NaN values
        nan_count = df_elv.T[0].isna().sum()
        if nan_count > 0:
            QgsMessageLog.logMessage(
                f"WARNING: {nan_count} land cover classes have NaN elevation",
                "QSIGA_CAL",
                Qgis.Warning
            )

        return df_elv.T

