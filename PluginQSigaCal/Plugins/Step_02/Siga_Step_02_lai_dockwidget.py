    def DownloadLAI_MODIS(self, PathOutputs, PathRaster, start_date, end_date):
        """
        Download MODIS LAI data using LAADS DAAC AppKey authentication.
        Supports incremental downloads - existing files are skipped.
        Automatically splits large date ranges into manageable chunks.

        AppKey must be stored in: {project_folder}/temp/earthdata_token.txt
        Get your AppKey from: https://ladsweb.modaps.eosdis.nasa.gov/profile/
        """
        try:
            # Parse dates first to check range
            start = datetime.strptime(start_date, '%Y-%m-%d')
            end = datetime.strptime(end_date, '%Y-%m-%d')

            # Calculate date range in years
            years_range = (end - start).days / 365.25

            # If range > 1 year, split into 1-year blocks for better reliability
            # Based on testing, year-by-year downloads work more consistently
            if years_range > 1:
                return self._download_in_blocks(PathOutputs, PathRaster, start, end, block_years=1)

            # Otherwise, proceed with single download
            return self._download_single_period(PathOutputs, PathRaster, start, end)

        except Exception as e:
            QMessageBox.critical(self, "Download Error",
                               f"Failed to download MODIS data.\n\n" +
                               f"Error: {str(e)}\n\n" +
                               "Check your internet connection and AppKey.")
            import traceback
            traceback.print_exc()

    def _download_in_blocks(self, PathOutputs, PathRaster, start_date, end_date, block_years=1):
        """
        Download MODIS data in blocks for large date ranges.
        Each block shows its own alert and progress bar.
        """
        from dateutil.relativedelta import relativedelta

        blocks = []
        current_start = start_date

        # Create blocks
        while current_start < end_date:
            current_end = min(
                current_start + relativedelta(years=block_years),
                end_date
            )
            blocks.append((current_start, current_end))
            current_start = current_end

        total_blocks = len(blocks)
        total_years = (end_date - start_date).days / 365.25

        # Inform user about block processing
        reply = QMessageBox.question(
            self,
            "Large Date Range Detected",
            f"Date range: {total_years:.1f} years ({start_date.strftime('%Y-%m-%d')} to {end_date.strftime('%Y-%m-%d')})\n\n" +
            f"The download will be split into {total_blocks} periods.\n" +
            f"You will see a progress dialog for each period.\n\n" +
            f"Existing files will be automatically skipped.\n\n" +
            "Continue with download?",
            QMessageBox.Yes | QMessageBox.No
        )

        if reply == QMessageBox.No:
            return

        # Process each block with individual alerts and progress bars
        total_downloaded = 0
        total_failed = 0

        for block_num, (block_start, block_end) in enumerate(blocks, 1):
            # Show alert for this block/year
            year_str = block_start.strftime('%Y')
            QMessageBox.information(
                self,
                f"Downloading Block {block_num}/{total_blocks}",
                f"Starting download for year {year_str}\n\n" +
                f"Period: {block_start.strftime('%Y-%m-%d')} to {block_end.strftime('%Y-%m-%d')}\n\n" +
                "Please wait while files are downloaded..."
            )

            # Download this block with dialogs enabled (shows progress bar)
            result = self._download_single_period(PathOutputs, PathRaster, block_start, block_end, show_dialogs=True)

            if result:
                total_downloaded += result.get('downloaded', 0)
                total_failed += result.get('failed', 0)

        # Final summary
        actual_files = glob.glob(os.path.join(PathOutputs, "*.hdf"))
        actual_count = len(actual_files)

        QMessageBox.information(
            self,
            "All Downloads Complete",
            f"All {total_blocks} periods processed!\n\n" +
            f"Total period: {start_date.strftime('%Y-%m-%d')} to {end_date.strftime('%Y-%m-%d')}\n" +
            f"Files in folder: {actual_count}\n" +
            f"New downloads: {total_downloaded}\n" +
            f"Failed: {total_failed}\n\n" +
            "You can now proceed to Step 2 - Process MODIS Data."
        )

        return {'downloaded': total_downloaded, 'failed': total_failed, 'total': actual_count}

    def _download_single_period(self, PathOutputs, PathRaster, start, end, show_dialogs=True):
        """
        Download MODIS data for a single time period.
        This is the core download logic extracted from the original method.

        Args:
            show_dialogs: If False, suppress QMessageBox dialogs (for block processing)
        """
        try:
            tiles = self.raster_to_modis_tiles(PathRaster)
            product = "MOD15A2H"
            collection = "61"

            # LAADS DAAC base URL
            base_url = f"https://ladsweb.modaps.eosdis.nasa.gov/archive/allData/{collection}/{product}/"

            # Read AppKey from token file
            project_path = os.path.join(self.output_path.text(), self.name_project.text())
            token_file = os.path.join(project_path, 'temp', 'earthdata_token.txt')

            if not os.path.exists(token_file):
                QMessageBox.critical(self, "AppKey Not Found",
                                   f"NASA Earthdata AppKey file not found.\n\n" +
                                   f"Expected location:\n{token_file}\n\n" +
                                   "To get your AppKey:\n" +
                                   "1. Visit: https://ladsweb.modaps.eosdis.nasa.gov/profile/\n" +
                                   "2. Login with your Earthdata credentials\n" +
                                   "3. Click 'Generate Token' to get your AppKey\n" +
                                   "4. Save the AppKey in a text file at the location above\n\n" +
                                   "The token file should contain only the AppKey (one line).")
                return

            # Read AppKey
            try:
                with open(token_file, 'r') as f:
                    app_key = f.read().strip()

                if not app_key:
                    raise ValueError("Token file is empty")

            except Exception as e:
                QMessageBox.critical(self, "Invalid AppKey File",
                                   f"Failed to read AppKey from:\n{token_file}\n\n" +
                                   f"Error: {str(e)}\n\n" +
                                   "Make sure the file contains your NASA Earthdata AppKey.")
                return

            # Create session with AppKey authentication
            session = requests.Session()
            headers = {'Authorization': f'Bearer {app_key}'}
            session.headers.update(headers)

            # Generate list of dates (every 8 days for MOD15A2H)
            date_list = []
            current_date = start
            while current_date <= end:
                date_list.append(current_date)
                current_date += timedelta(days=8)

            if not date_list:
                QMessageBox.warning(self, "No Dates", "No valid dates in the specified range.")
                return

            # Check for existing files
            existing_files = glob.glob(os.path.join(PathOutputs, "*.hdf"))
            existing_count = len(existing_files)

            # Collect files to download (skip existing ones)
            files_to_download = []

            for date in date_list:
                year = date.strftime('%Y')
                doy = date.strftime('%j')  # Day of year
                date_url = f"{base_url}{year}/{doy}/"

                try:
                    response = session.get(date_url, timeout=30)

                    if response.status_code == 200:
                        html = response.text

                        # Check for authentication failure
                        if 'Earthdata Login' in html or '<title>Login' in html:
                            QMessageBox.critical(self, "Authentication Failed",
                                               "NASA Earthdata authentication failed.\n\n" +
                                               "Your AppKey may be invalid or expired.\n\n" +
                                               "Please update your token file with a new AppKey from:\n" +
                                               "https://ladsweb.modaps.eosdis.nasa.gov/profile/")
                            return

                        # Find all HDF files matching our tiles
                        for tile in tiles.split(','):
                            tile = tile.strip()
                            # Pattern: MOD15A2H.A2010001.h10v09.061.*.hdf
                            pattern = f'{product}\\.A{year}{doy}\\.{tile}\\.[0-9]+\\.[0-9]+\\.hdf'
                            matches = re.findall(pattern, html)

                            # Remove duplicates (HTML may list files multiple times)
                            matches = list(set(matches))

                            for filename in matches:
                                output_path = os.path.join(PathOutputs, filename)

                                # Skip if file already exists and is valid (incremental download)
                                if os.path.exists(output_path) and os.path.getsize(output_path) > 50000:
                                    continue

                                file_url = urljoin(date_url, filename)
                                files_to_download.append({
                                    'url': file_url,
                                    'filename': filename,
                                    'date': date.strftime('%Y-%m-%d')
                                })

                except:
                    pass  # Silently skip errors during file discovery

            # Check if we need to download anything
            if not files_to_download:
                date_range_str = f"{start.strftime('%Y-%m-%d')} to {end.strftime('%Y-%m-%d')}"
                if existing_count > 0:
                    # Don't show message for block downloads, just return stats
                    print(f"Block {date_range_str}: All {existing_count} files already exist, skipping.")
                else:
                    print(f"Block {date_range_str}: No new data found.")

                # Return stats even when nothing to download
                return {'downloaded': 0, 'failed': 0, 'total': existing_count}

            # Download files
            total_files = len(files_to_download)
            self.progressBar_Download.setMaximum(total_files)

            if show_dialogs:
                msg = f"Found {total_files} new files to download.\n\n"
                if existing_count > 0:
                    msg += f"Existing files (will be skipped): {existing_count}\n\n"
                msg += "Please keep the window open until complete."
                QMessageBox.information(self, "Download Starting", msg)
            else:
                # Just print to console for block downloads
                print(f"Downloading {total_files} new files... (Existing: {existing_count})")

            downloaded = 0
            failed = 0

            for i, file_info in enumerate(files_to_download):
                try:
                    file_url = file_info['url']
                    filename = file_info['filename']
                    output_path = os.path.join(PathOutputs, filename)

                    # Download file
                    response = session.get(file_url, stream=True, timeout=60)

                    if response.status_code == 200:
                        # Check if we got HTML (authentication error) instead of binary data
                        content_type = response.headers.get('Content-Type', '')

                        if 'text/html' in content_type:
                            # Authentication failed, got HTML instead of HDF
                            failed += 1
                            continue

                        with open(output_path, 'wb') as f:
                            for chunk in response.iter_content(chunk_size=8192):
                                if chunk:  # filter out keep-alive chunks
                                    f.write(chunk)

                        # Verify file was downloaded and has content (at least 50KB for valid HDF)
                        if os.path.exists(output_path) and os.path.getsize(output_path) > 50000:
                            downloaded += 1
                        else:
                            failed += 1
                            if os.path.exists(output_path):
                                os.remove(output_path)  # Remove invalid file
                    else:
                        failed += 1

                except Exception as e:
                    failed += 1

                self.progressBar_Download.setValue(i + 1)

            # Summary - count actual files in folder
            self.progressBar_Download.setValue(total_files)

            actual_files = glob.glob(os.path.join(PathOutputs, "*.hdf"))
            actual_count = len(actual_files)

            if show_dialogs:
                summary_msg = (f"Download Complete!\n\n" +
                              f"Attempted: {total_files}\n" +
                              f"Successfully downloaded: {downloaded}\n" +
                              f"Failed: {failed}\n" +
                              f"Previously existing: {existing_count}\n\n" +
                              f"Total HDF files in folder: {actual_count}\n\n" +
                              f"Output folder:\n{PathOutputs}")

                if failed > 0:
                    QMessageBox.warning(self, "Download Complete with Errors", summary_msg)
                else:
                    QMessageBox.information(self, "Download Complete", summary_msg)
            else:
                # Just print summary for block downloads
                print(f"Block completed: Downloaded {downloaded}, Failed {failed}, Total in folder: {actual_count}")

            # Return statistics for block processing
            return {'downloaded': downloaded, 'failed': failed, 'total': actual_count}

        except Exception as e:
            QMessageBox.critical(self, "Download Error",
                               f"Failed to download MODIS data.\n\n" +
                               f"Error: {str(e)}\n\n" +
                               "Verify credentials and data availability at:\n" +
                               "https://ladsweb.modaps.eosdis.nasa.gov/search/")

    def raster_to_modis_tiles(self, PathRaster, sample_size=100):
        def lonlat_to_modis_tile(lon, lat):
            tile_width = 10.0
            tile_height = 10.0
            lon_min = -180.0
            lat_max = 90.0
            h = int(np.round((lon - lon_min) / tile_width, 0))
            v = int(np.round((lat_max - lat) / tile_height, 0))
            return h, v

        from pyproj import Transformer, CRS
        with rasterio.open(PathRaster) as src:
            crs = src.crs
            transform = src.transform
            width = src.width
            height = src.height
            sample_coords = [(random.randint(0, height - 1), random.randint(0, width - 1)) for _ in range(sample_size)]
            coords = [rasterio.transform.xy(transform, row, col, offset='center') for row, col in sample_coords]

        if not crs.is_geographic:
            transformer = Transformer.from_crs(crs, CRS.from_epsg(4326), always_xy=True)
            coords = [transformer.transform(x, y) for x, y in coords]

            # proj_raster = Proj(crs)
            # proj_wgs84 = Proj(init="epsg:4326")
            # coords = [pyproj.transform(proj_raster, proj_wgs84, x, y) for x, y in coords]

        modis_tiles = set(lonlat_to_modis_tile(lon, lat) for lon, lat in coords)
        return ','.join([f"h{h:02d}v{v:02d}" for h, v in modis_tiles])

