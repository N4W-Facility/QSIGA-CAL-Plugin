class StreamToLogger:
    def __init__(self, progress_signal):
        self.progress_signal = progress_signal

    def write(self, buf):
        for line in buf.splitlines():
            if line.strip():
                self.handle_line(line)

    def handle_line(self, line):
        # Busca líneas que contengan "of" y actualiza el progreso
        if "of" in line:
            try:
                parts = line.split(',')
                iter_info = parts[0].strip().split()
                iter_num = int(iter_info[0])
                total_num = int(iter_info[2])
                progress = int((iter_num / total_num) * 100)
                self.progress_signal.progress.emit(progress)
            except (ValueError, IndexError):
                pass

    def flush(self):
        pass
