    def _find_column_case_insensitive(self, dataframe, column_name):
        """
        Find a column in a dataframe with case-insensitive matching.

        Args:
            dataframe: pandas/geopandas DataFrame
            column_name: target column name (e.g., 'codigo')

        Returns:
            Actual column name from dataframe

        Raises:
            KeyError: if column not found
        """
        # Create mapping of lowercase column names to actual names
        column_map = {col.lower(): col for col in dataframe.columns}

        # Search for target column (case-insensitive)
        target_lower = column_name.lower()

        if target_lower in column_map:
            return column_map[target_lower]
        else:
            raise KeyError(
                f"Column '{column_name}' not found in shapefile.\n\n" +
                f"Available columns: {', '.join(dataframe.columns)}\n\n" +
                f"Please make sure your stations shapefile has a column\n" +
                f"named 'codigo', 'Codigo', or 'CODIGO' with station IDs."
            )

