    def load_project_state(self):
        """
        Load the plugin state from QGIS project variables.
        This is called automatically when a QGIS project is opened.
        """
        try:
            project = QgsProject.instance()

            QgsMessageLog.logMessage("QSIGA_CAL Step 02: Loading project state...", "QSIGA_CAL", Qgis.Info)

            # Read state from project variables
            state_json, success = project.readEntry('QSIGA_CAL_Step02', 'plugin_state')

            if not success or not state_json:
                QgsMessageLog.logMessage("QSIGA_CAL Step 02: No saved state found", "QSIGA_CAL", Qgis.Warning)
                return

            state = json.loads(state_json)

            QgsMessageLog.logMessage(f"Found saved state with {len(state)} items", "QSIGA_CAL", Qgis.Info)

            # Restore Step 1 - Download MODIS
            if 'name_project' in state and state['name_project']:
                self.name_project.setText(state['name_project'])
            if 'output_path' in state and state['output_path']:
                self.output_path.setText(state['output_path'])

            # Support both old (Path_Dem) and new (Path_raster) field names
            if 'Path_raster' in state and state['Path_raster']:
                self.Path_raster.setText(state['Path_raster'])
            elif 'Path_Dem' in state and state['Path_Dem']:  # Backward compatibility
                self.Path_raster.setText(state['Path_Dem'])

            if 'start_date' in state and state['start_date']:
                self.start_date.setDate(QDate.fromString(state['start_date'], 'yyyy-MM-dd'))
            if 'end_date' in state and state['end_date']:
                self.end_date.setDate(QDate.fromString(state['end_date'], 'yyyy-MM-dd'))

            # Restore Step 2 - Process MODIS
            if 'Path_Lulc' in state and state['Path_Lulc']:
                self.Path_Lulc.setText(state['Path_Lulc'])
            if 'Path_Shp_Stations' in state and state['Path_Shp_Stations']:
                self.Path_Shp_Stations.setText(state['Path_Shp_Stations'])
            if 'Path_excel_time_series' in state and state['Path_excel_time_series']:
                self.Path_excel_time_series.setText(state['Path_excel_time_series'])
            if 'Path_WS' in state and state['Path_WS']:
                self.Path_WS.setText(state['Path_WS'])

            # Restore Step 3 - Calibration spinBox selection
            # Need to populate spinBox first before restoring selection
            if 'current_toolbox_index' in state and state['current_toolbox_index'] == 2:
                # User was on calibration page, restore spinBox values
                try:
                    self.update_spinBox_values()  # Populate spinBox with available coverages

                    # Restore selected coverage by text (more reliable than index)
                    if 'spinBox_currentText' in state and state['spinBox_currentText']:
                        index = self.spinBox.findText(state['spinBox_currentText'])
                        if index >= 0:
                            self.spinBox.setCurrentIndex(index)
                            QgsMessageLog.logMessage(f"Restored spinBox selection: {state['spinBox_currentText']}", "QSIGA_CAL", Qgis.Info)
                    elif 'spinBox_currentIndex' in state and state['spinBox_currentIndex'] >= 0:
                        # Fallback to index if text not available
                        if state['spinBox_currentIndex'] < self.spinBox.count():
                            self.spinBox.setCurrentIndex(state['spinBox_currentIndex'])
                except Exception as e:
                    QgsMessageLog.logMessage(f"Could not restore spinBox selection: {e}", "QSIGA_CAL", Qgis.Warning)

            # Restore current toolbox page
            if 'current_toolbox_index' in state:
                self.toolBox.setCurrentIndex(state['current_toolbox_index'])

            msg = f"Project state loaded: {state.get('name_project', 'Unknown')}"
            QgsMessageLog.logMessage(msg, "QSIGA_CAL", Qgis.Success)
            print(f"QSIGA_CAL Step 02: {msg}")

        except Exception as e:
            error_msg = f"Error loading Step 02 project state: {e}"
            QgsMessageLog.logMessage(error_msg, "QSIGA_CAL", Qgis.Critical)
            print(error_msg)
            import traceback
            traceback.print_exc()

