    def create_raster_filled_with_value(self, output_path, value):
        with rasterio.open(self.Path_Dem.text()) as src:
            meta = src.meta.copy()
            data = src.read(1)
            data.fill(value)

            with rasterio.open(output_path, 'w', **meta) as dst:
                dst.write(data, 1)

    def create_sequential_raster(self, input_raster_path, output_raster_path):
        with rasterio.open(input_raster_path) as src:
            meta = src.meta.copy()
            data = src.read(1)
            unique_values = np.unique(data)
            value_map = {old: new for new, old in enumerate(unique_values, start=1)}

            for old, new in value_map.items():
                data[data == old] = new

            with rasterio.open(output_raster_path, 'w', **meta) as dst:
                dst.write(data, 1)

