    def toggle_base_files(self):
        """Enable/disable base files controls based on checkbox state."""
        is_checked = self.Check_Base_File.isChecked()
        self.select_base_files.setEnabled(is_checked)
        self.path_base_files.setEnabled(is_checked)

        # If unchecked, clear the path
        if not is_checked:
            self.path_base_files.clear()

    def select_base_files_folder(self):
        """Open dialog to select base files folder."""
        folder = QtWidgets.QFileDialog.getExistingDirectory(self, "Select Base Files Folder")
        if folder:
            self.path_base_files.setText(folder)
            self.show_message("Success", f"Base files folder set to: {folder}")

    def get_base_csv_path(self, subfolder=''):
        """
        Get the base CSV path based on user selection.

        Parameters:
        -----------
        subfolder : str
            Subfolder within base path (e.g., 'muestreador', 'calibracion')

        Returns:
        --------
        str
            Full path to the base CSV folder
        """
        if self.Check_Base_File.isChecked() and self.path_base_files.text():
            # Use external folder
            base_path = self.path_base_files.text()
            if subfolder:
                return os.path.join(base_path, subfolder)
            return base_path
        else:
            # Use internal archivos_csv folder
            base_path = os.path.join(os.path.dirname(__file__), 'archivos_csv')
            if subfolder:
                return os.path.join(base_path, subfolder)
            return base_path

