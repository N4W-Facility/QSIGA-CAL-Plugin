        #     callback=self.run,
        #     parent=self.iface.mainWindow())

    def unload(self):
        """Remove the plugin menu item and icon from QGIS GUI."""
        for action in self.actions:
            self.iface.removePluginMenu(self.tr(u'&QSIGA_CAL'), action)
            self.iface.removeToolBarIcon(action)
        del self.toolbar

    def run(self):
        """Run method to start the plugin."""
        if self.dialog is None:
            self.dialog = QSigaCalDialog(self.iface)  # Pass iface to the dialog

        # Show the dialog window
        self.dialog.show()
        # Bring the dialog to the front
        self.dialog.raise_()
        self.dialog.activateWindow()

    def open_step_1(self):
        """Cargar el subplugin Step 1."""
        LOGGER.info("Opening Step 1 subplugin.")
        self.step_1_dock = Siga_Step_01_ConfigDockWidget(self.iface)
        self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.step_1_dock)
