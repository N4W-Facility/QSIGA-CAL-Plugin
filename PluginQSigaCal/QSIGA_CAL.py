class QSIGA_CAL:
    """QGIS Plugin Implementation."""
    def __init__(self, iface):
        """Constructor for the plugin."""
        self.iface = iface  # Save reference to the QGIS interface
        self.dockwidget = None
        self.dialog = None  # Variable to store the main dialog instance
        self.actions = []
        self.menu = self.tr(u'&QSIGA_CAL')
        self.toolbar = self.iface.addToolBar(u'QSIGA_CAL')
        self.toolbar.setObjectName(u'QSIGA_CAL')

    def tr(self, message):
        """Get the translation for a string using Qt translation API."""
        return QCoreApplication.translate('QSIGA_CAL', message)

    def add_action(
            self, icon_path, text, callback, enabled_flag=True, add_to_menu=True,
            add_to_toolbar=True, status_tip=None, whats_this=None, parent=None):
        """Add an action to the plugin's toolbar."""
        icon = QIcon(icon_path)
        action = QAction(icon, text, parent)
        action.triggered.connect(callback)
        action.setEnabled(enabled_flag)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            self.toolbar.addAction(action)

        if add_to_menu:
            self.iface.addPluginToMenu(self.menu, action)

        self.actions.append(action)

        return action

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        plugin_dir = os.path.dirname(__file__)
        icon_path = os.path.join(plugin_dir, 'logos', 'Qsiga.png')  # Cambiar el logo si es necesario

        self.add_action(
            icon_path,
            text=self.tr(u'QSIGA_CAL Configuration'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # icon_path = ':/plugins/QSIGA_CAL/icon.png'
