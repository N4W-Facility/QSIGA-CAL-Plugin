        if embalse_path:
            embalse_raster_path = os.path.join(self.output_path.text(), self.name_project.text(), 'temp',
                                               'Reservoir.tif')
            PolyponToRaster(PathShp=embalse_path, PathOutput=embalse_raster_path, PathRaster=dem_path, Value='Id')
        else:
            embalse_raster_path = None

        # Definir el criterio basado en las selecciones de los CheckBoxes
        criteria = 1  # Default criteria
        if self.Check_Nbs.isChecked() and self.Check_Embalse.isChecked() and self.Check_Cabeceras.isChecked():
            criteria = 4
        elif self.Check_Embalse.isChecked() and self.Check_Cabeceras.isChecked():
            criteria = 3
        elif self.Check_Embalse.isChecked():
            criteria = 2

        from .functions.BuildingTopology import build_topology

        try:
            build_topology(dem_path, flow_dir_path, flow_accum_path, embalse_raster_path, cabeceras_path, nbs_path,
                           x_coor, y_coor, name_scenario,name_project, output_folder, criteria)

            # Copiar el archivo de topología a la ruta especificada
            from shutil import copyfile
            topology_file = os.path.join(output_folder, f'Topology_{name_project}.txt')
            output_pth = os.path.join(self.output_path.text(), self.name_project.text(), 'entradas', 'topologia')
            if not os.path.exists(output_pth):
                os.makedirs(output_pth)
            copyfile(topology_file, os.path.join(output_pth, f'Topology_{name_project}.txt'))

            self.show_message("Success", "Topology processes completed successfully.")
        except Exception as e:
            self.show_message("Error", f"Topology process failed: {e}")

