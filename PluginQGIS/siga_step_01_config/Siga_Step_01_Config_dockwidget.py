    def run_dem_processes(self):
        """Run DEM processes and save results."""
        dem_path = self.Path_Dem.text()
        threshold = float(self.Threshold.text())
        output_folder = os.path.join(self.output_path.text(), self.name_project.text(), 'temp',
                                     '01_Delimitacion_Cuenca')

        if not os.path.exists(output_folder):
            os.makedirs(output_folder)

        grid = Grid.from_raster(dem_path, data_name='dem')
        raw_dem = grid.read_raster(dem_path)

        # flooded_dem = grid.fill_depressions(raw_dem)
        # dem = grid.resolve_flats(flooded_dem)
        dem = raw_dem
        dirmap = (8, 9, 6, 3, 2, 1, 4, 7)
        flow_dir = grid.flowdir(dem, dirmap=dirmap)

        flow_accum = grid.accumulation(flow_dir, dirmap=dirmap)

        pixel_area = (grid.viewfinder.dy_dx[0] * grid.viewfinder.dy_dx[0]) / 1E6
        accum_area = flow_accum * pixel_area
        network = (accum_area > threshold) * 1

        # Save results
        dem_path = os.path.join(output_folder, 'DEM.tif')
        flow_dir_path = os.path.join(output_folder, 'FlowDir.tif')
        flow_accum_path = os.path.join(output_folder, 'FlowAccum.tif')
        network_path = os.path.join(output_folder, 'Network.tif')

        grid.to_raster(dem, dem_path)
        grid.to_raster(flow_dir, flow_dir_path)
        grid.to_raster(flow_accum, flow_accum_path)
        grid.to_raster(network, network_path)

        # Load FlowAccum and Network into QGIS
        flow_accum_layer = QgsRasterLayer(flow_accum_path, "Flow Accumulation")
        network_layer = QgsRasterLayer(network_path, "Network")

        # if flow_accum_layer.isValid():
        #     QgsProject.instance().addMapLayer(flow_accum_layer)

        if network_layer.isValid():
            shader = QgsRasterShader()
            color_ramp = QgsColorRampShader()
            color_ramp.setColorRampType(QgsColorRampShader.Interpolated)
            color_ramp_item_list = [QgsColorRampShader.ColorRampItem(1, QColor(0, 0, 255))]
            color_ramp.setColorRampItemList(color_ramp_item_list)
            shader.setRasterShaderFunction(color_ramp)
            renderer = QgsSingleBandPseudoColorRenderer(network_layer.dataProvider(), 1, shader)
            network_layer.setRenderer(renderer)
            QgsProject.instance().addMapLayer(network_layer)

    def run_basin_processes(self):
        """Run basin delineation processes."""
        x_coor = float(self.X_coor.text())
        y_coor = float(self.Y_coor.text())
        output_folder = os.path.join(self.output_path.text(), self.name_project.text(), 'temp',
                                     '01_Delimitacion_Cuenca')

        dem_path = os.path.join(output_folder, 'DEM.tif')
        flow_dir_path = os.path.join(output_folder, 'FlowDir.tif')

        grid = Grid.from_raster(dem_path, data_name='dem')
        flow_dir = grid.read_raster(flow_dir_path)

        col, row = grid.nearest_cell(x_coor, y_coor, snap="center")
        x_new = grid.extent[0] + (col * grid.viewfinder.dy_dx[0])
        y_new = grid.extent[3] - (row * grid.viewfinder.dy_dx[0])

        watershed = grid.catchment(x=x_new, y=y_new, fdir=flow_dir, dirmap=(8, 9, 6, 3, 2, 1, 4, 7),
                                   xytype='coordinate')
        watershed = watershed * 1

        # Save and load watershed
        watershed_path = os.path.join(output_folder, 'Watershed.tif')
        grid.to_raster(watershed, watershed_path)

        watershed_shapefile = os.path.join(output_folder, 'Watershed.shp')
        processing.run("gdal:polygonize", {
            'INPUT': watershed_path,
            'BAND': 1,
            'FIELD': 'DN',
            'EIGHT_CONNECTEDNESS': False,
            'EXTRA': '',
            'OUTPUT': watershed_shapefile
        })

        watershed_layer = QgsVectorLayer(watershed_shapefile, "Watershed", "ogr")
        if watershed_layer.isValid():
            symbol = QgsFillSymbol.createSimple({'color_border': 'red', 'width_border': '1', 'style': 'no'})
            watershed_layer.renderer().setSymbol(symbol)
            QgsProject.instance().addMapLayer(watershed_layer)
        else:
            self.show_message("Error", "No se pudo cargar la capa Watershed.")

