        # Procesar Movimientos en Masa
        excel_path_mov_mass = self.Path_excel_Mov_mass.text()
        if excel_path_mov_mass:
            df = pd.read_excel(excel_path_mov_mass, engine='openpyxl')

            # Asegurarse de que la columna de fecha se transforme en Año, Mes y Día
            if 'Fecha' in df.columns:
                df['Fecha'] = pd.to_datetime(df['Fecha'])
                df['Año'] = df['Fecha'].dt.year
                df['Mes'] = df['Fecha'].dt.month
                df['Día'] = df['Fecha'].dt.day
                df.drop(columns=['Fecha'], inplace=True)

            # Reordenar columnas para asegurar que Año, Mes y Día estén al principio
            column_order = ['Año', 'Mes', 'Día'] + [col for col in df.columns if col not in ['Año', 'Mes', 'Día']]
            df = df[column_order]

            # Crear el diccionario necesario
            dic_movmasa = {
                'n_deslizamientos': str(len(df)),
                'datos_faltantes': '-9999',
                'datos': df
            }
            # Crear el archivo de salida
            ficheros.crear_archivo_movmasa(dic_movmasa)

        # Procesar Vertimientos
        excel_path_vertimientos = self.Path_excel_Vertimientos.text()
        if excel_path_vertimientos:
            df_vertimientos = pd.read_excel(excel_path_vertimientos, engine='openpyxl')

