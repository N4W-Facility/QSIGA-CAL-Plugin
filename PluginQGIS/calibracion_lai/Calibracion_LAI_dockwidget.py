    def process_modis_data(self, destination_folder, reference_raster):
        hdf_files = glob.glob(os.path.join(destination_folder, "*.hdf"))

        date_files = {}
        for hdf_file in hdf_files:
            date_str = os.path.basename(hdf_file).split('.')[1][1:8]
            if date_str not in date_files:
                date_files[date_str] = []
            date_files[date_str].append(hdf_file)

        total_files = len(date_files)
        self.progressBar_modis.setMaximum(total_files)

        for i, (date_str, files) in enumerate(date_files.items()):
            vrt_path = os.path.join(destination_folder, f"mosaic_{date_str}.vrt")
            temp_raster = os.path.join(destination_folder, f"temp_raster_{date_str}.tif")
            output_raster = os.path.join(destination_folder, f"MODIS_{date_str}.tif")

            self.convert_mosaic_to_raster(files, vrt_path, temp_raster, subdataset_name='Lai_500m')
            self.resample_to_match(temp_raster, reference_raster, output_raster)

            print(f"Exportado raster: {output_raster}")

            os.remove(vrt_path)
            self.progressBar_modis.setValue(i + 1)

        self.progressBar_modis.setValue(total_files)

    def convert_mosaic_to_raster(self,hdf_files, vrt_path, output_raster, subdataset_name='Lai_500m'):
        subdatasets = [f'HDF4_EOS:EOS_GRID:"{f}":MOD_Grid_MOD15A2H:{subdataset_name}' for f in hdf_files]
        vrt_options = gdal.BuildVRTOptions(separate=False)
        vrt = gdal.BuildVRT(vrt_path, subdatasets, options=vrt_options)

        # Convertir el archivo VRT a un raster GeoTIFF
        gdal.Translate(output_raster, vrt, format='GTiff')

    def resample_to_match(self, src_path, match_path, dst_path):
        with rasterio.open(src_path) as src:
            src_transform = src.transform
            src_crs = src.crs
            src_width = src.width
            src_height = src.height

            with rasterio.open(match_path) as match:
                match_transform = match.transform
                match_crs = match.crs
                match_width = match.width
                match_height = match.height

                if src_crs != match_crs:
                    src_crs = match_crs
                    src_transform, src_width, src_height = calculate_default_transform(
                        src_crs, match_crs, src_width, src_height, *src.bounds)

                kwargs = src.meta.copy()
                kwargs.update({
                    'crs': match_crs,
                    'transform': match_transform,
                    'width': match_width,
                    'height': match_height
                })

                with rasterio.open(dst_path, 'w', **kwargs) as dst:
                    for i in range(1, src.count + 1):
                        reproject(
                            source=rasterio.band(src, i),
                            destination=rasterio.band(dst, i),
                            src_transform=src_transform,
                            src_crs=src_crs,
                            dst_transform=match_transform,
                            dst_crs=match_crs,
                            resampling=Resampling.nearest)

