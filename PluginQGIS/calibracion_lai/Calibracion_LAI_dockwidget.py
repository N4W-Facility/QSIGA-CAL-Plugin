    def calculate_moving_average(self, input_excel_path, output_excel_path, window_size):
        df = pd.read_excel(input_excel_path, index_col='Date', parse_dates=True)
        df = df.fillna(df.mean())

        reconstructed_df = pd.DataFrame(index=df.index)

        for col in df.columns[1:]:
            LAI_data = df[col]
            result = seasonal_decompose(LAI_data, model='additive', period=46)
            trend = result.trend
            seasonal = result.seasonal
            reconstructed_series = trend + seasonal
            reconstructed_df[col] = reconstructed_series

        moving_averages = reconstructed_df.rolling(window=window_size).mean()
        moving_averages.to_excel(output_excel_path, engine='openpyxl')

