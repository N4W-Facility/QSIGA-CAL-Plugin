    def temperatura(self, Elevacion, Series_ts, progress_signal=None):
        fechas = Series_ts.columns[3:]

        Series_ts[Series_ts == -999] = np.NAN
        data = Series_ts[fechas].interpolate(axis=1)

        x = Series_ts['z']
        X = sm.add_constant(x, prepend=False)

        Cuencas = Elevacion.index
        df_result = pd.DataFrame(columns=Cuencas, index=fechas)

        total_steps = len(fechas)
        if progress_signal:
            progress_signal.progress.emit(0)

        for i, f in enumerate(fechas):
            print(f)
            model = sm.OLS(data[f], X, missing='drop')
            statistics = model.fit()
            coefficient_A = statistics.params[0]
            coefficient_B = statistics.params[1]

            df_ele_aligned = Elevacion.reindex(Cuencas)
            values = coefficient_A * df_ele_aligned[0] + coefficient_B
            df_result.loc[f] = values

            if progress_signal:
                progress_signal.progress.emit(int((i + 1) / total_steps * 100))

        return df_result

    def IDW(self, x, y, z, nx, ny, xmax, xmin, ymax, ymin, power=2):
        def distancia(x, y, xi, yi):
            obs = np.vstack((x, y)).T
            interpolar = np.vstack((xi, yi)).T
            d0 = np.subtract.outer(obs[:, 0], interpolar[:, 0])
            d1 = np.subtract.outer(obs[:, 1], interpolar[:, 1])
            Md = np.hypot(d0, d1)
            return Md

        def malla(xmax, xmin, ymax, ymin, nx, ny):
            xi = np.linspace(xmin, xmax, nx)
            yi = np.linspace(ymin, ymax, ny)
            xi, yi = np.meshgrid(xi, yi)
            xi, yi = xi.flatten(), yi.flatten()
            return xi, yi

        xi, yi = malla(xmax, xmin, ymax, ymin, nx, ny)
        dist = distancia(x, y, xi, yi)
        W = 1.0 / dist**power
        W /= W.sum(axis=0)
        zi = np.dot(W.T, z)
        zi = zi.reshape(nx, ny)
        return xi, yi, zi

    def pixel(self, xmax, xmin, ymax, ymin, nx, ny):
        pixelWidth = abs(xmin - xmax) / nx
        pixelHeight = abs(ymin - ymax) / ny
        return pixelWidth, pixelHeight

