    def zonal_statistics(self, coverage_raster_path, modis_directory, output_excel_path):
        with rasterio.open(coverage_raster_path) as src:
            coverage_data = src.read(1)
            coverage_meta = src.meta

        coverage_codes = np.unique(coverage_data)
        coverage_codes = coverage_codes[coverage_codes != coverage_meta['nodata']]

        lai_series = {code: [] for code in coverage_codes}
        dates = []

        modis_files = sorted([f for f in os.listdir(modis_directory) if f.startswith('MODIS_') and f.endswith('.tif')])
        total_files = len(modis_files)
        self.progressBar_Zonal.setMaximum(total_files)

        for i, modis_file in enumerate(modis_files):
            date_str = modis_file.split('_')[1].split('.')[0]
            year = int(date_str[:4])
            julian_day = int(date_str[4:])
            date = self.julian_to_date(year, julian_day)
            dates.append(date)
            modis_path = os.path.join(modis_directory, modis_file)

            with rasterio.open(modis_path) as src:
                modis_data = src.read(1)
                modis_meta = src.meta

            for code in coverage_codes:
                mask = coverage_data == code
                if np.any(mask):
                    lai_mean = np.mean(modis_data[mask])
                    lai_series[code].append(lai_mean)
                else:
                    lai_series[code].append(np.nan)

            self.progressBar_Zonal.setValue(i + 1)

        Factor = 0.1
        df = pd.DataFrame(lai_series , index=pd.to_datetime(dates))* Factor
        df.index.name = 'Date'
        df.to_excel(output_excel_path, engine='openpyxl')
        self.progressBar_Zonal.setValue(total_files)

