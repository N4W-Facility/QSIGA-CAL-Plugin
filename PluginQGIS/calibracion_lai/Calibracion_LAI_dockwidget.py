    def Series_X_Y(self, catalogo, df, var):
        if var == 'TAmax' or var == 'TAmin':
            cols_select = ['y', 'x', 'z']
        else:
            cols_select = ['y', 'x']

        data = df.loc[df.index[0]:].T
        columnas = data.columns
        str_list = [d.strftime('%Y-%m-%d') for d in columnas]
        data.index = [int(d1) for d1 in data.index]
        data.columns = str_list
        df_new = catalogo[cols_select]
        data.index.name = 'codigo'
        df_new.index = [int(d2) for d2 in df_new.index]
        df_new = df_new.merge(data, left_index=True, right_index=True)

        return df_new

