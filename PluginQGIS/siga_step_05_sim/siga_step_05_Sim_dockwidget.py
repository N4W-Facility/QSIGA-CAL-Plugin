    def toggle_path_edit(self):
        """
        Habilita o deshabilita el QLineEdit según el QRadioButton seleccionado.
        También puede llamar a una función específica dependiendo de la selección.
        """
        if self.Yes.isChecked():
            self.LULC_PATH.setEnabled(True)  # Habilitar el QLineEdit
        else:
            self.LULC_PATH.setEnabled(False) # Deshabilitar el QLineEdit

    def select_portafolio_data(self):

        dem_path, _ = QFileDialog.getOpenFileName(self, "select file LULC", "", "Files .TIF (*.tif)")
        if dem_path:
            self.LULC_PATH.setText(dem_path)  # Actualizar QLineEdit Path_Dem
            self.load_raster_layer(dem_path, "LULC")

    def load_raster_layer(self, path, layer_name):
        """Load raster layer into QGIS."""
        layer = QgsRasterLayer(path, layer_name)
        if not layer.isValid():
            QMessageBox.critical(self, "Error", f"File could not be loaded {layer_name}.")
        else:
            QgsProject.instance().addMapLayer(layer)

    def Copy_maps_new_escenario(self):


        name_project = self.name_project.text()
        name_scenario_i = self.name_scenario_initial.text()
        Name_Basin = self.Name_Basin.text()
        new_escenario = self.name_scenario_Nbs.text()
        output_path = self.output_path.text()


        origen = os.path.join(output_path, name_project, 'entradas', 'mapas', name_scenario_i)

        destino = os.path.join(output_path, name_project, 'entradas', 'mapas', new_escenario)
        if self.Yes.isChecked():
            lista_archivos = ['arcillas.tif', 'arenas.tif', 'idCiclo.tif', 'idQ.tif', 'ki.tif', 'limos.tif', 'MO.tif',
                              'P_usle.tif'
                , 'paramo.tif', 'prioridad.tif', 'Q5a.tif', 'transicion.tif', 'Zs.tif', 'K_usle.tif']
        else:
            lista_archivos = ['arcillas.tif', 'arenas.tif', 'idCiclo.tif', 'idQ.tif', 'ki.tif', 'limos.tif', 'MO.tif',
                              'P_usle.tif'
                , 'paramo.tif', 'prioridad.tif', 'Q5a.tif', 'transicion.tif', 'Zs.tif', 'K_usle.tif','CLC.tif']
        os.makedirs(destino, exist_ok=True)

        archivos_copiados = []
        archivos_no_encontrados = []

        # Recorrer la lista de archivos especificada
        for archivo in lista_archivos:
            ruta_archivo_origen = os.path.join(origen, archivo)

            try:
                # Verificar si el archivo existe en el origen
                if os.path.exists(ruta_archivo_origen):
                    # Copiar el archivo al destino
                    shutil.copy2(ruta_archivo_origen, destino)
                    archivos_copiados.append(archivo)
                    print(f"Archivo '{archivo}' copiado exitosamente a {destino}.")
                else:
                    archivos_no_encontrados.append(archivo)
                    QtWidgets.QMessageBox.warning(self, "Error", f"file {archivo} not found.")

            except Exception as e:
                QtWidgets.QMessageBox.warning(self, "Error", f"An error occurred while copying the file  {archivo} not found.{e}")

        QtWidgets.QMessageBox.information(self, "Copy maps Files","All the .tif files were copied.")

