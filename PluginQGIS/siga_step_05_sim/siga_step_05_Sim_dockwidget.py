    def select_output_folder(self):
        # Abrir un diálogo para seleccionar la carpeta de salida
        output_path = QtWidgets.QFileDialog.getExistingDirectory(self, "Select Output Folder")
        if output_path:
            self.output_path.setText(output_path)

    def create_folders(self):
        # Obtener los valores de los campos de texto
        name_project = self.name_project.text()
        name_scenario = self.name_scenario.text()
        Name_Basin = self.Name_Basin.text()
        output_path = self.output_path.text()

        if name_project and output_path:
            # Definir las rutas
            NameProcessCal = os.path.join(output_path, name_project, 'temp', '05-Prioritization')

            # Crear las carpetas necesarias
            os.makedirs(NameProcessCal, exist_ok=True)
            folders = ['DATA','FIGURES','RESULTS']
            for folder in folders:
                new_filder = os.path.join(output_path, name_project, 'temp', '04-Calibration',folder)
                os.makedirs(new_filder, exist_ok=True)

            # Llamar a la función para crear las carpetas de salida
            create_outputs_folders(output_path, name_project, name_scenario)

            ruta_origen = os.path.join(os.path.dirname(__file__), 'bin', 'cnvrtr.exe')

            ruta_origen_exe = os.path.join(os.path.dirname(__file__), 'bin', 'SIGA.exe')
            ruta_destino = output_path + os.path.sep

            # Copiar el archivo necesario
            copiar_archivo(ruta_origen, ruta_destino)

            copiar_archivo(ruta_origen_exe, ruta_destino)

            QtWidgets.QMessageBox.information(self, "Success", "Folders created successfully!")
            # Move to the next widget
            self.toolBox.setCurrentIndex(1)
        else:
            QtWidgets.QMessageBox.warning(self, "Input Error", "Please provide project name and output path.")

